			<h2>Insolvenzrecht</h2>
			
			
			

<p>Neues Insolvenzrecht, Verbraucherinsolvenzrecht, Restschuldbefreiung</p>

<p>Das neue Verbraucherinsolvenzverfahren wird auf natürliche Personen angewandt und ist<br />
auf Personen beschränkt, die keine bzw. nur eine geringfügige wirtschaftlich selbständige<br />
Tätigkeit ausüben ( Minderkaufmann § 4 I HGB ).</p>

<p>Das Verfahren ist dreistufig.</p>

<p>Das neue Insolvenzrecht gilt prinzipiell ab dem 01.01.1999. Wegen dem zwingend vorher<br />
durchzuführenden außergerichtlichen Einigungsverfahren kann es bereits ab dem 01.07.98 angewandt werden.</p>

<p>Das Verbraucherinsolvenzrecht hat drei Stufen, die nacheinander durchlaufen werden müssen.</p>

<p><b>I.</b></p>

<p>Außergerichtliche Schuldenbereinigung:</p>

<p>Der Schuldner hat gem. § 305 I 1 InsO mit Hilfe einer geeigneten Person oder Stelle<br />
diesen Versuch vorzunehmen. Geeignete Personen dürften in erster Linie Rechtsanwälte<br />
und Notare sein.</p>

<p>In Betracht kommen auch Schuldnerberatungsstellen; da diese Stellen jetzt schon völlig<br />
überlaufen sind, werden sie in der Praxis keine allzu große Rolle spielen.</p>

<p>1. Die Einbindung der geeigneten Person oder Stelle ist zwingend vorgeschrieben.</p>

<p>2. Dieser Einigungsversuch muß auf Grundlage eines Planes durchgeführt werden. Die Aus-<br />
gestaltung des Planes scheint zur Zeit noch relativ frei.</p>

<p>In der Literatur ist derzeit die Problematik des Nullplanes ( an die Gläubiger aus-<br />
geschüttete Quote = Null ) höchst umstritten. Derzeit soll sie zulässig sein.</p>

<p>Für den Plan sollten in der Praxis unbedingt taktische Erwägungen berücksichtigt<br />
werden.</p>

<p>Zum Zeitpunkt des Planes durchgeführte Zwangsvollstreckungen können lediglich nach<br />
§ 765 a ZPO abgeblockt werden. Nach der derzeitigen Rechtssprechung des BGH bestehen<br />
keine allzu große Erfolgsaussichten; eine Rechtssprechung die dringend reformbe-<br />
dürftig ist.</p>

<p>3. Die geeignete Person oder Stelle, die den Schuldenbereinigungsplan aufgestellt hat,<br />
erstellt eine Abschlußbescheinigung für den Schuldner, in der über die Eingungsver-<br />
handlungen berichtet wird und die außergerichtlichen Einigungsversuche geschildert<br />
werden. Hier liegt es nahe die Gläubiger zu benennen, die eine außergerichtliche<br />
Einigung verhindert haben.</p>

<p><b>II.</b></p>

<p>Die zweite Stufe des Verfahrens stellt das gerichtliche Insolvenzverfahren dar.<br />
Grundsätzlich zuständig ist das Amtsgericht des Schuldnerwohnsitzes. Voraussetzungen<br />
für das gerichtliche Insolvenzverfahren sind:</p>

<p>1. Eine Antragsberechtigung. Der Schuldner selbst kann den Antrag stellen.</p>

<p>2. Der außergerichtliche Einigungsversuch.</p>

<p>3. Die Einhaltung der 6-Monatsfrist nach § 305 InsO.</p>

<p>4. Ein Eröffnungsgrund nach § 16 InsO in der Regel also Zahlungsunfähigkeit.<br />
§ 18 Insolvenzordnung führt einen neuen Eröffnungsgrund, nämlich die drohende<br />
Zahlungsunfähigkeit ein.</p>

<p>5. Der Schuldner muß die notwendigen Unterlagen beibringen. Hierzu gehören:</p>

<p>a. die sogenannte Abschlußbescheinigung ( siehe oben )<br />
b. der Antrag auf Erteilung einer Restschuldbefreiung<br />
c. die Abtretungserklärung: hier sollen alle pfändbaren Einkünfte und Forderungen,<br />
die der Schuldner erhält für die Zeit von 7 Jahren nach Aufhebung des Insolvenz-<br />
verfahrens an ein vom Gericht zu bestimmenden Treuhänder abgetreten werden.</p>

<p>Wer Treuhänder sein soll ist derzeit noch nicht ganz geklärt. Zu denken ist hierbei an<br />
Schöffen und andere Personen. Wegen möglicher Interessenkollission, aber auch wegen des<br />
maximal angedachten Gebührensatzes von DM 30,&#8212;pro Stunde werden hier kaum Rechtsan-<br />
wälte auftreten ( mit einem Stundensatz von DM 30,&#8212;kann kein Büro angemietet und<br />
Personal bezahlt werden ). Angesichts der Fülle der Funktionen, die der Treuhänder aus-<br />
zufüllen hat und der Verantwortung die auf ihm lastet, erscheint ein Stundensatz von<br />
DM 30,&#8212;verglichen mit dem Stundensatz eines Bauhilfsarbeiters von DM 50,&#8212;- DM 60,&#8212;,<br />
wenig angemessen.</p>

<p>In diesem Zusammenhang überrascht § 114 InsO mit völliger Unsystematik. Danach sollen<br />
Abtretungen noch drei Jahre gelten nachdem das Insolvenzverfahren begonnen hat. Stellt<br />
man dies rechtskräftigen Titeln gegenüber, die nach dem Beginn des Insolvenzverfahrens<br />
nicht mehr durchsetzbar sind, ist diese Regelung unverständlich, zumal sie in der<br />
Regel lediglich Banken bevorteilen dürfte. Hierin liegt vielleicht letztlich auch<br />
der Grund für diese Regelung.</p>

<p>- Der Schuldner muß ein vollständiges Gläubiger- und Forderungsverzeichnis vorlegen,<br />
dessen Vollständigkeit zu versichern ist.</p>

<p>- Auch in der zweiten Stufe des Verfahrens gibt es den Schuldenbereinigungsplan.<br />
In diesem Zusammenhang ist darauf hinzuweisen, daß unterschiedliche Quoten mög-<br />
lich sind. Auch streitige Verbindlichkeiten sind aufzuführen. Sie sind zu be-<br />
werten ( im Zweifel wohl mit Null ). Unklar sind die laufenden Verbindlichkeiten.<br />
Allein Mietforderungen sind hier im Gesetz geklärt. Es fehlen Regelungen für Strom,<br />
Telefon, Gas und Wasser. Es ist derzeit fraglich, ob der Treuhänder diese Verträge<br />
kündigen kann.</p>

<p><b>III.</b></p>

<p>Das gerichtliche Insolvenzverfahren wird lediglich auf Antrag des Schuldners durchgeführt.<br />
Sollte ein Gläubigerantrag vorliegen, was oftmals seitens AOK oder Finanzamt (erfahrungs-<br />
gemäß) erfolgt, sollte der Schuldner ebenfalls einen Antrag stellen, um in die Vorzüge<br />
des Insolvenzverfahrens zu kommen.</p>



<p><b>IV.</b></p>

<p>Nach Antragsstellung ordnet das Gericht gem. § 305 InsO das Ruhen des Verfahrens an.<br />
Die verschiedenen Gläubiger werden mit einer Notfrist von 1 Monat zur Stellung-<br />
nahme aufgefordert. Sollte seitens der Gläubiger keine Reaktion erfolgen, so gilt<br />
Schweigen als Zustimmung.</p>

<p>Die fehlende Gläubigerzustimmung kann durch das Gericht ersetzt werden, dies innerhalb<br />
bestimmter gesetzlicher Grenzen.</p>

<p><br />
Erfolgen keine Einwendungen gegen den Schuldenbereinigungsplan ( im weiteren spp )<br />
gilt er gemäß § 308 I 1 InsO als angenommen.</p>

<p><br />
<b>VI.</b></p>

<p>Scheitert das Verfahrens über den Schuldenbereinigungsplan, kann es zum vereinfachten<br />
Insolvenzverfahren kommen. Dieses Verfahren kann mit nur einem Prüfungstermin<br />
schriftlich durchgeführt werden.</p>

<p>1. Nach § 313 I 2 InsO soll der bei Eröffnung des Verfahrens bestellten Treuhänder,<br />
nun die Aufgaben eines Insolvenzverwalters übernehmen die gem. § 313 II und III<br />
InsO beschränkt sind.</p>

<p>Der Schuldner kann dem Gericht einen Treuhänder vorschlagen, wobei auch Familienange-<br />
hörige Treuhänder sein können.</p>

<p>2. Mit Verfahrenseröffnung kann nicht mehr mit befreiender Wirkung an den Schuldner be-<br />
zahlt werden;<br />
Zwangsvollstreckungsmaßnahmen sind unzulässig,<br />
Miet- und Pachtverhältnisse können vom Treuhänder nach § 109 InsO ohne Einhaltung der<br />
gesetzlichen Kündigungsfristen gekündigt werden.</p>

<p>3. Die Insolvenzmasse wird durch den Treuhänder verwertet und verteilt. Ist ausreichende<br />
Masse nicht vorhanden, kann der Treuhänder beantragen, daß von der Verwertung ganz oder<br />
teilweise abgesehen wird und der Schuldner an den Treuhänder den entsprechenden Wert<br />
bezahlt.</p>

<p>Genügt die verwertbare Masse nicht, um die Verfahrenskosten zu decken, stellt das Insol-<br />
venzgericht gem. § 207 I InsO das Verfahren ein. Es kommt dann zu keiner Restschuldbe-<br />
freiung.</p>

<p><b>VII.</b></p>

<p>Die dritte Stufe des Verfahrens ist die Restschuldbefreiung.</p>

<p><b>VIII.</b></p>

<p>Kernstück des neuen Insolvenzrechts und der Reform ist die sogenannte Restschuldbe-<br />
freiung, die dritte Stufe des Verfahrens.</p>

<p>1. Nach Abschluß des Insolvenzverfahrens trifft das Gericht die Entscheidung, ob dem<br />
Schuldner Restschuldbefreiung gewährt wird. Hiervor ist eine Anhörung der Gläubiger<br />
vorgeschrieben.</p>

<p>2. Anhörung der Gläubiger:<br />
(Zu dem Thema soll dem Schuldner eine 7-jährige Phase mit Restschuldbefreiung gewährt<br />
werden ?)<br />
Ein wichtiges Kriterium wird das bisherige Verhalten des Schuldners bei Auskünften und<br />
Mitwirkung im Verfahren sein.</p>

<p>Gläubiger können den Antrag auf Versagung der Restschuldbefreiung im Anhörungstermin<br />
stellen. Sie haben entsprechende Versagungsgründe glaubhaft zu machen.</p>

<p>§ 290 I InsO legt einen Katalog vor, nachdem zwingend bei Antrag eines Gläubigers die<br />
Restschuldbefreiung zu versagen ist. Hierzu gehören insbesondere Verletzung von Buch-<br />
führungspflichten, Bankrott etc. Interessant ist in diesem Zusammenhang das Verfahren<br />
der Konkursverschleppung nach dem GmbHG und Verstöße der Gesellschaftergeschäftsführer<br />
gegen das GmbHG, die hier nicht aufgeführt sind.</p>

<p>Selbstverständlich berechtigen grobfahrlässige unrichtige und unvollständige Angaben des<br />
Schuldners über seine wirtschaftlichen Verhältnisse, um einen Kredit zu erhalten oder<br />
Leistungen aus öffentlichen Mitteln zu beziehen, ebenfalls zur Versagung.</p>

<p>````<br />
Nichtberechtigen,<br />
sondern Zwingen zur Versagung bei Antragsstellung durch den Gläubiger.<br />
````</p>

<p>Gleiches gilt für vorsätzliche grobfahrlässige Verletzung von Auskunfts- und Mitwirkungs-<br />
pflichten des Schuldners während des Insolvenzverfahrens.</p>

<p>Die Entscheidung des Gerichts zur Restschuldbefreiung kann mit einer Notfrist von<br />
2 Wochen mit sofortiger Beschwerde angegriffen werden.</p>

<p><b>IX.</b></p>

<p>An die Entscheidung des Gerichts schließt sich die 7-jährige Wohlverhaltensperiode<br />
an.</p>

<p>Im Übergangszeitraum kann diese Periode nur 5 Jahre dauern ( Artikel 107 EG-InsO ),<br />
sofern der Schuldner bereits vor dem 01.01.1997 zahlungsunfähig war und er dies<br />
glaubhaft machen kann.</p>

<p>1. Als wichtigstes Kriterium für das Wohlverhalten besteht die Erwerbsobligenheit nach<br />
der der Schuldner jede zumutbare Tätigkeit annehmen muß, wobei zumutbar auch Aushilfs-<br />
tätigkeiten sind.</p>

<p>Der Schuldner hat beispielsweise nachzuweisen, daß er Anzeigen geschaltet hat; er muß<br />
seine Bewerbungen dokumentieren. Letztlich dürfte dies dem gesteigerten Pflichten beim<br />
Unterhaltsrecht für den Unterhaltsschuldner entsprechen.</p>

<p>Ein Kindererziehungsjahr - auch für den Mann - ist zulässig, da dies ein gesetzliches<br />
zulässiges Verfahren ist.</p>

<p>Derzeit umstritten ist, ob der Schuldner auch dann eine Tätigkeit annehmen muß, wenn<br />
die Bezüge aus der neuen Tätigkeit unter den Arbeitslosengeldbezügen der alten Tätig-<br />
keit liegen. Da hiermit letztendlich Gläubiger geschädigt würden, gehe ich davon aus,<br />
daß dies nicht verlangt werden kann.</p>

<p>Das Verfahren endet mit dem positiven Fall der Restschuldbefreiung, die gegen alle In-<br />
solvenzgläubiger, auch solche Gläubiger, die ihre Forderungen nicht angemeldet haben,<br />
gilt.</p>

<p>Wichtig ist, daß beispielsweise die Ehefrau eines Schuldners, die Restschuldbefreiung<br />
nicht zu ihren Gunsten anführen kann, so daß bei einem Kredit den Ehefrau und Schuldner<br />
abgeschlossen haben, gegen sie vorgegangen werden kann. Im Innenverhältnis kann die<br />
Ehefrau nicht gegen den Ehemann vorgehen.</p>

<p>Ähnlich ist es für Bürgen geregelt. Der Bürge haftet nach wie vor und kann im Innen-<br />
verhältnis aber nicht gegen den Schuldner vorgehen.
</p>



 

				
				
