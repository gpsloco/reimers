			<h2>Bußgeld und Blitzer</h2>
			
			
			

<p>Ob Abstandsmessungen per Videokamera- zumeist auf deutschen Autobahnen durchgeführt- überhaupt zur Bestrafung führen können, ist durch Urteil des Bundesverfassungsgerichts -weitgehend unerwartet- sehr strittig geworden. Das gilt z.T. auch für klassische „Blitzer“, soweit Fotos durch Lichtschrankentechnik aufgenommen werden ( so jedenfalls das AG GRIMMA vom 31.08.2009,Az. 3 OWI 166 JS 35228/09).<br />
Im Folgenden möchte ich hierzu die wichtigsten Meinungen und Urteile zusammenfassen, um am Ende meine Rechtsauffassung und diesbezügliche Erfahrung mit der Justiz in Bayern darzustellen.<br />
Seit dem Entscheid des Bundesverfassungsgerichts vom 11.8.2009 ist klargestellt, dass die Polizei bei jeder Art von Bild- und Videoaufzeichnungen eine gesetzliche Rechtsgrundlage braucht.<br />
Nun will die Polizei natürlich weiterhin ihre Aufgabe der Verkehrskontrolle und - Überwachung durchführen und nicht zuletzt Geld in die Staatskasse spülen. Die &#8220;Tagesausbeute&#8221; nur eines Abstandsmessgeräts liegt schließlich weit jenseits der 50.000 Euro-Marke.<br />
Damit nicht alle Bußgeldbescheide rechtswidrig sind, müssen sich Verwaltung und Justiz die Frage stellen, auf welche Rechtsgrundlage die Abstandsmessungen sich stützen können. Hier kommt nicht viel in Betracht- letztlich nur:<br />
1.:§ 81 b StPO: kann hier aber nicht Rechtsgrundlage sein, da die aufgeführten Maßnahmen nur dann zulässig sind, sofern ein Beschuldigter (Betroffener) zu diesem geworden ist, was also allenfalls erst nach Ermittlung des Fahrzeughalters vorliegen kann. <br />
2. § 163 b StPO wird selbst von den Gerichten kaum in Betracht gezogen, weil die Vorschrift schlicht nicht passt.<br />
3.:§ 100 h StPO: Dieser Paragraph wird am liebsten als Rechtsgrundlage angeführt. Dabei wird- v.a. von bayerischen Gerichten- gerne ignoriert, dass noch keine höchstrichterliche Rechtsprechung hierzu besteht (das BVerfG hat in seinem Urteil NUR gesagt, dass jedenfalls Verwaltungsrichtlinien NICHT als Rechtsgrundlage dienen können) und die Literatur nahezu einhellig der Meinung ist, dass bei Abstands- oder Geschwindigkeitsmessungen, die mit Bildaufzeichnungen arbeiten, § 100 h StPO NICHT einschlägig ist (Vgl. Meyer-Goßner, StPO, 50. Aufl., zu § 100f a.F.; Lampe, in: Kommentar zum OWiG, 3. Aufl. § 46 Rn. 27a; Wolter, SK StPO, § 100h, Rn.4). Argumentation ist dabei zumeist, dass eine Anwendung des § 100h wegen des Eingriffs in das Persönlichkeitsrechts des Betroffenen (Recht am eigenen Bild; datenschutzrechtliche Erwägungen) ausscheidet. <br />
Und selbst wenn man diese Norm als Rechtsgrundlage betrachten wollte, dann müsste bei der jeweiligen polizeilichen Bildaufzeichnung bereits ein konkreter Tatverdacht bestanden haben, d.h. dass die Polizei bei Einschaltung der Videokamera bereits einen Verdacht haben muss, dass eine Ordnungswidrigkeit vorliegt.<br />
Das ist gerade bei Abstandsmessverfahren problematisch:<br />
Denn hier läuft (mindestens) eine Videokamera i.d.R. auf Dauerbetrieb. Da hierbei gerade kein konkreter Tatverdacht besteht, ergibt sich hieraus ein Beweiserhebungsverbot. Dies führt zu einem Beweisverwertungsverbot, da es sich bei der zur Last gelegten Tat &#8220;nicht um eine Straftat von erheblicher Bedeutung, sondern nur um eine Ordnungswidrigkeit gehandelt hat, so dass das Interesse des Staates an der funktionierenden Strafrechtspflege hinter dem Grundrecht des Betroffenen auf informationelle Selbstbestimmung zurücktreten muss&#8221;, so das AG Lünen und andere.<br />
Lediglich die Bayern entscheiden dennoch stur gegen diesen übermächtigen Trend in Rechtsprechung und Literatur:<br />
So verurteilt das LG Schweinfurt (Urteil vom 31.08.2009, 12 OWi 17 Js 7822/09) den &#8220;Abstandssünder&#8221; dennoch. Dabei stellt es darauf ab, dass das &#8220;Bayerische Brückenabstandsmessverfahren&#8221; nicht vergleichbar mit den sonstigen Videoaufzeichungsverfahren sei.<br />
Grund hierfür soll sein, dass zwar hier 2 Kameras auf der Brücke dauerhaft aufzeichnen, das Bild des Fahrers und des Nummernschilds aber erst auf Hinweis eines Polizeibeamten mit einer dritten Kamera am Straßenrand aufgenommen wird.<br />
Diese Argumentation ist zumindest fraglich; denn selbst wenn die dritte Kamera-Aufnahme tatsächlich rechtmäßig war, so ist der Verstoß erst mit den Daueraufzeichnungen der Brückenkameras feststellbar. Und diese sind wiederum nach herrschender Meinung rechtswidrig- mit der Folge eines Beweiserhebungs- und Verwertungsverbots; darauf geht das LG Schweinfurt aber nicht ein.<br />
Letztlich haben es bayerische Amtsgerichts- Richter derzeit schwer. Einerseits dürften viele von ihnen zumindest Bedenken bei diesen Argumenten haben; Andererseits haben sie den Druck der höheren bayerischen Rechtsprechung und wollen sich nicht mit anderslautenden Urteilen die Finger verbrennen.<br />
Vor Kurzem hatte ich Termin in Erlangen- Es ging auch hier um eine bayerische Brücken-Abstandsmessung; da das Foto etwas undeutlich war, hoffte ich, dass der Richter schon deshalb &#8220;in dubio pro reo&#8221; einstellt oder freispricht;  dies ist zwar zumindest in Bayern heutzutage eher selten geworden. Wenn man aber den Druck auf den Richtern beachtet, so dürfte dies ein wieder salonfähiger Weg sein, um an der Problematik in diesem Bereich vorbeizukommen- vorausgesetzt natürlich, der Richter hat wirklich Zweifel an der Idendität des Betroffenen. <br />
Natürlich hatte ich beim Gericht bereits vor der Verhandlung unter Hinweis auf die Rechtsprechung des BverfG um Einstellung gebeten, diese wurde abgelehnt weil es sich hier &#8220;wohl nicht um eine Daueraufzeichung gehandelt haben dürfte&#8221;. Der Richter dürfte aber meine Hartnäckigkeit hinsichtlich meiner Rechtsauffassung gespürt haben.<br />
Meine Einschätzung war zum Glück richtig: der Richter stellte das Verfahren gem. § 47 Abs. 2 OWiG ohne weitere Angabe von Gründen ein. Verhandlungsdauer ca. 2 Minuten.<br />
Dass auch ein Freispruch möglich gewesen wäre, ist klar; mir ist es aber egal, warum mein Mandant NICHT verurteilt wird und auch sonst keine Kosten zu tragen hat.</p>

