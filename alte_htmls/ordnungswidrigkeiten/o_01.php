			<h2>Kein Fahrverbot trotz regelmäßigem Cannabiskonsum</h2>
			
			
			

<p>Auf einer Feier am 26.03.11 hatte mein Mandant Gras geraucht oder um in den Fachtermini zu bleiben: Sich eines berauschenden Mittels der Cannaboide bedient.<br />
Zwei Tage später kam er in eine Verkehrskontrolle. Die Blutentnahme ergab einen Wert von 1,3 ng/ml.<br />
Das löste Aktivitäten der Justiz aus. Zum einen die der Führerscheinbehörde, ob er seinen Führerschein auf Probe behalten dürfe; zum anderen die des Staatsanwalts, der ein Betäubungsmittelverfahren eröffnete. Später wurde dieses Verfahren (vernünftigerweise ) in ein OWi- Verfahren abgewandelt. In diesem Verfahren erging ein Bußgeldbescheid mit Fahrverbot und Bußgeld von €&nbsp; 839,81.<br />
Der Einspruch hiergegen war erfolgreich. Im Termin wurde der Sachverständige<br />
Prof. Dr. H. S. aus Gießen gehört. Der erklärte: Der ermittelte Wert läge zwar an der Nachweisbarkeitsgrenze, sei mit 1,3 aber noch klar positiv. Ein zweiter Cannaboidwert von 31,8 mücrogramm pro Liter weise daraufhin, dass der Konsum etwa einmal die Woche erfolge, dies allerdings in Maßen.<br />
Auf die Frage des Richters hin, ob dies nicht eine physische oder psychische Abhängigkeit des Betroffenen zur Folge habe, erklärte der Sachverständige klar und deutlich: Nein, dies sei nicht der Fall.<br />
Bei dem hier festgestellten Richtwert zwei Tage nach Konsum sei auch ganz klar davon auszugehen, dass der betroffene Angeklagte weder objektiv noch subjektiv hätte merken können, dass er ein Fahrzeug nicht mehr führen kann. Wohl gemerkt,<br />
das heißt: <br />
- subjektiv konnte der Angeklagte nichts mehr spüren ( was nur der Angeklagte selbst wissen kann ), <br />
- objektiv ergäbe sich aber auch kein Anhaltspunkt dafür, dass das Führen eines Kfz irgendwie noch risikobehaftet sei.<br />
Daraufhin stellte das Gericht das Verfahren nach § 47 Abs. 2 OWiG ohne jede weitere Bedingungen ein; Punkte und Fahrverbot entfielen.<br />
P. S. Der Ausgang des Verfahrens erfreute nicht nur den Betroffenen. Nach den nicht nachvollziehbaren Entscheidungen des Amtsgerichts Würzburg ( <a href="http://goo.gl/tFuwY">http://goo.gl/tFuwY</a> ) hatte auch in diesem Verfahren das Gericht vorher schriftlich angekündigt, dass es keine Veranlassung sehe, den Bußgeldbescheid abzuändern. Noch am Tag der Verhandlung hatte der Richter persönlich vorher angerufen, ob denn der Weg nach Bad Hersfeld wirklich Sinn mache…!<br />
Der Weg hat Sinn gemacht, das war erfreulich. <br />
Siehe auch &nbsp; <a href="http://goo.gl/WzfWX">http://goo.gl/WzfWX</a><br />
Az.: AG Bad Hersfeld 70 OWi - 32 Js 17178/11
</p>



 

				
				
