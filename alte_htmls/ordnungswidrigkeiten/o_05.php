			<h2>OWi-Recht / Bußgeld</h2>
			
			
			

<p> Nach Verurteilung in Bußgeldsachen durch den Amtsrichter sind Beschwerden zum Bayrischen Obersten Landesgericht selten erfolgreich. Manchmal doch:<br />
Der Mandant war bei vorgeschriebener Geschwindigkeit ins Schleudern gekommen. Das Fahrzeug hatte sich dabei überschlagen. Ein Fahrzeuginsasse wurde verletzt.</p>

<p>Der Bußgeldbescheid betrug 115,00 Euro und war mit 3 Punkten nach der Flensburger Kartei verbunden.</p>

<p>Nach der mündlichen Verhandlung reduzierte das Amtsgericht die Strafe auf 60,00 Euro. Das Amtsgericht verurteilte den Mandanten deshalb, weil er trotz Einhalten der vorgeschriebenen Geschwindigkeit nicht richtig reagiert habe. Indem er zu stark gebremst habe, hätten die Reifen des Fahrzeugs blockiert. Mit einer Stotterbremsung wäre es nicht zum Fahrzeugüberschlag gekommen.</p>

<p>Hiergegen habe ich eingewendet, daß kaum ein Fahrzeugführer bei der mit dem überraschend einsetzenden Rutschen des Fahrzeugs sofort notwendig werdenden Stotterbremsung überfordert ist. Nicht umsonst würden deshalb in neuere Fahrzeuge Anti-Blockier-Systeme eingebaut. Der Einbau erfolgt letztendlich u.a. deshalb, weil man weiß, daß in solchen Situationen wenig Fahrzeugführer richtig reagieren.</p>

<p>Deshalb handelte, nach meinem Dafürhalten, der Mandant nicht fahrlässig, wenn er - so wie hier geschehen - bei angepaßter Geschwindigkeit - Gegenlenken und Bremsen reagierte.</p>

<p>Das Bayrische Oberste hatte ein Einsehen und stellte das Verfahren gegen meinen Mandanten (ohne Punkte und Strafe) ein.
</p>



 

				
				
