			<h2>Video _nicht_ Beweis bei Geschwindigkeitsüberschreitung</h2>
			
			
			

<p>02.12.11</p>

<p>Erfolg in Bußgeldsachen ist ein steiniger Weg. 						<br />
Hier ging es über einen Gutachten. Dem Mandanten wurde vorgeworfen, außerorts<br />
die zulässige Höchstgeschwindigkeit um 24 Km/h überschritten zu haben. Dies war durch Nachfahren bewiesen worden. Ein entsprechender Videofilm war vorhanden.<br />
Bei Durchsicht des Videos drängte sich mir der Eindruck auf, dass hier kein konstanter Abstand gehalten wurde. Deshalb wurde ein Gutachten in Auftrag gegeben. Das Gutachten ergab tatsächlich, dass der notwendige feste Abstand nicht eingehalten worden war und die Messung deshalb unbrauchbar war.Damit lag m.E nach keine nachweisbare OWI  mehr vor. Weil der Mandant aber keine mündliche Verhandlung wollte, wurde unter dem AZ: 2020 Js 19723/11 13 OWI  dann quasi im Vergleichswege (in einem tatsächlichen &#8220;Vergleich&#8221;) Folgendes vereinbart:</p>

<p><b>Beschluss</b> </p>

<p>In der Bußgeldsache</p>

<p>gegen XXXXXXXXXXXX</p>

<p>Verteidiger: Rechtsanwalt Thilo Reimers, Rosengasse 8, 97070 Würzburg</p>

<p>wegen Verkehrsordnungswidrigkeit</p>

<p>hat das Amtsgericht Montabaur durch den Richter XXXXXXXXX am 03.11.2011</p>

<p>beschlossen:</p>

<p>Gegen den Betroffenen wird wegen einer fahrlässigen Verkehrsordnungswidrigkeit <br />
- Überschreitung der zulässigen Höchstgeschwindigkeit außerorts um 24 Km/h - eine Geldbuße in Höhe von 70,00 €&nbsp; festgesetzt.</p>

<p>Der Betroffene trägt die Kosten des Verfahrens und seine/ihre notwendigen Auslagen.</p>

<p>Gründe:</p>

<p>Gegen d. Betroffenen ist am 26.10.2010 ein Bußgeldbescheid ergangen. Die Geldbuße wurde auf 80,00 €&nbsp; festgesetzt.</p>

<p>Der.Betroffene hat hiergegen form- und fristgerecht Einspruch eingelegt. Das Gericht hält zur Entscheidung über den Einspruch eine Durchführung der Hauptverhandlung für nicht erforderlich, so dass vorliegend durch Beschluss entschieden werden kann<br />
( § 72 Abs. 1 OWiG ).<br />
Der hierzu angehörte Betroffene hat dieser Vorgehensweise zugestimmt.</p>

<p>Der entscheidungserhebliche Sachverhalt erscheint bereits aufgrund des vorliegenden Akteninhalts als hinreichend geklärt.</p>

<p>Wegen der Einzelheiten wird auf den Bußgeldbescheid Bezug genommen.</p>

<p>Diese Feststellungen beruhen auf der Auswertung des gesamten zur Verfügung stehenden Akteninhalts.</p>

<p>Nach den getroffenen Feststellungen hat der Betroffene eine fahrlässige Ordnungswidrigkeit gem. §§ 41, 49 StVO, 24 StVG, 11.3.4 BKat, begangen.</p>

<p>Zur Ahndung dieser Ordnungswidrigkeit hielt das Gericht unter Heranziehung der gesamten Umstände und gemessen am Grad der Vorwerfbarkeit eine Geldbuße von<br />
70,00 €&nbsp; für erforderlich und ausreichend.</p>

<p>Die Kostenentscheidung folgt aus §§ 46 Abs. 1 OWiG, 465 Abs. 1 StPO.</p>

<p>56410 Montabaur, 03.11.2011<br />
Das Amtsgericht</p>

<p>gez. XXXXXXXXXXXX, Richter</p>





 

				
				
