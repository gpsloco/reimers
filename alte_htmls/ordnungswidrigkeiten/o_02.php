			<h2>Keine Ordnungshaft wegen Nichterscheinens vor Gericht</h2>
			
			
			

<p>Mein Mandant M. liegt im Sterben. Er war Gesellschafter und Geschäftsführer einer insolventen GmbH.<br />
In einem Prozess des Insolvenzverwalters gegen andere Geschäftsführer dieser insolventen GmbH soll mein Mandant als Zeuge aussagen.</p>

<p>Dabei erging folgender Beschluss:</p>

<p>Dem Zeugen M. wird wegen nicht ausreichender Entschuldigung des Nichterscheinens im Termin vom 07.06.10 ein Ordnungsgeld in Höhe von €&nbsp; 200,&#8212;, <br />
ersatzweise zwei Tage Ordnungshaft auferlegt.</p>

<p>Gründe:</p>

<p>Es erscheint nicht glaubhaft, dass eine Ärztin in Würzburg die Hausärztin des Zeugen ist und die Verhandlungsunfähigkeit des Zeugen bestätigen kann.<br />
Der Zeuge hat sich geweigert, eine Schweigepflichtsentbindungserklärung zu erteilen.<br />
Die Attestierung durch Frau Dr. J. ist daher nicht ausreichend. Vielmehr liegt der Verdacht einer strafbaren Falschattestierung im Sinne von § 278 StGB vor.</p>

<p>Entrüstet zahlte M. nicht. Deshalb drohte ihm nun Ordnungshaft, die das Amtsgericht wohl totz Krankheit verhängt hätte.<br />
Hiergegen habe ich Beschwerde wegen greifbarer Gesetzeswidrigkeit des Beschlusses erhoben, deren Begründung ich zusammenfasse:</p>

<p>Das Amtsgericht verlangt vom Zeugen eine Entbindungserklärung zur ärztlichen Schweigepflicht zu unterschreiben. Ein Zeugnisverweigerungsrecht gilt auch und gerade gegenüber dem gesetzlichen Richter. Das Amtsgericht wähnt sich in einer Machtfülle, die nicht mit dem rechtsstaatlichen Demokratiegebot in Übereinstimmung zu bringen ist. </p>

<p>Zudem hat der Zeuge das Recht auf freie Arztwahl. Frau Dr. J. betreut den Mandanten seit Jahren.</p>

<p>Schließlich versteigt sich das Amtsgericht zu der Behauptung, es liege ein Gefälligkeitsattest vor. Wenn das Gericht meint, ein Gefälligkeitsattest liege vor, dann liegt eine Straftat vor. Wenn eine Straftat zur Kenntnis eines Richters gelangt, ist dieser gesetzlich verpflichtet Strafanzeige zu stellen. Das hat das Gericht nicht getan.<br />
Folglich zweifelt das Gericht das ärztliche Attest auch nicht an.</p>

<p>Das Landgericht Schweinfurt erklärt in seinem Beschluss vom 01.07.10:</p>

<p>Auf die sofortige Beschwerde hin wird von der Auferlegung eines Ordnungsgeldes abgesehen. </p>

<p>Zusammengefasst heißt es dort:</p>

<p>Die Intimsphäre des Zeugen ist zu schützen. Soweit er eine Weitergabe von detailhaften ärztlichen Attesten an die Parteien des Rechtsstaats verweigert, ist dem nachzukommen ( vgl. OLG Nürnberg, MDR 99, 315; OLG München MDR 2000,<br />
413 ). und ein Attest den Parteien nur vom Ergebnis her mitzuteilen und den Inhalt nicht aktenkundig zu machen.</p>

<p>Im begründeten Ausnahmefall könnte eine Zeuge auch verlangen, dass die krankheitsbedingte Ursache des Nichterscheinens auch dem Gericht gegenüber nicht offengelegt wird. Dies könnte durch Vorlage eines amtsärztlichen Attestes ( als neutrale Stelle ) geschehen, in welchem lediglich die Folgen ( Verhandlungs- bzw.<br />
Reiseunfähigkeit und deren voraussichtliche Dauer ) attestiert werden. </p>

<p>Nachdem seine lebensbedrohende Krankheit im Raum steht, sich die Auseinandersetzung mit dem Gericht im Laufe des Verfahrens beim Zeugen offenbar emotional hochschaukelte und er zumindest seiner Zeugnispflicht gem. § 377 Abs. 3 ZPO zweimal nachkam, sieht das Beschwerdegericht von der Auferlegung des Ordnungsgeldes in entsprechender Anwendung des § 47 OWiG ab ( vg. Münchener Kommentar, ZPO, 3. Aufl. Rdnr. 7 zu § 381 ).</p>

<p>AZ: 41 T 106/10
</p>



 

				
				
