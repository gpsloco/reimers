			<h2>Kein Kündigungsschutz beim Wechsel in die Betriebsgesellschaft</h2>
			
			
			

<p>15 Ca 1130/10 <br />
(Arbeitsgericht Nürnberg) <br />
 <br />
Die Parteien streiten über die Wirksamkeit einer betriebsbedingten Kündigung. <br />
 <br />
Die am 20.02.1959 geborene Klägerin ist seit 01.09.2009 bei der im Sommer 2009 neu <br />
gegründeten Beklagten als Reinigungskraft zuletzt zu einem monatlichen Bruttoentgelt von 1.771 EUR <br />
bei einer regelmäßigen wöchentlichen Arbeitszeit von 41 Stunden beschäftigt. Nach dem Arbeitsvertrag vom 07.09.2009 (Bl. 32 ff d.A.) gilt das Arbeitsverhält- <br />
nis kündigungsrechtlich seit dem 17.09.1990 als bestehend. <br />
 <br />
Seit diesem Zeitpunkt bis zu ihrem Wechsel zur Beklagten war die Klägerin bei der R… <br />
B… W… Hotelbetriebsgesellschaft mbH ebenfalls als Reinigungskraft beschäftigt. Tätigkeit und Ort <br />
der zu erbringenden Arbeitsleistung änderte sich mit dem Wechsel nicht.&nbsp; <br />
 <br />
Zuletzt beschäftigte die Beklagte weniger als zehn Mitarbeiter. <br />
 <br />
Mit Schreiben vom 25.01.2010, das der Klägerin am 29.01.2010 zugegangen ist, kündigte <br />
die Beklagte das Arbeitsverhältnis fristgerecht zum 31.07.2010 (Bl. 5 d. A.). <br />
 <br />
Mit ihrer Klage vom 19.02.2010, die am selben Tag beim Arbeitsgericht Nürnberg eingegangen ist, <br />
wandte sich die Klägerin gegen diese Kündigung. <br />
 <br />
Das Arbeitsgericht hat die Klage mit Urteil vom 26.08.2010 abgewiesen. <br />
 <br />
Das Arbeitsgericht hat sein Urteil damit begründet, dass das Kündigungsschutzgesetz auf <br />
die streitgegenständliche Kündigung nicht anwendbar sei, da die Beklagte nicht mehr als <br />
zehn Arbeitnehmer im Sinne von § 23 Abs. 1 KSchG zum Zeitpunkt der Kündigung be- <br />
schäftigt habe. Ebenso habe die Erörterung im Kammertermin ergeben, dass zum Zeit- <br />
punkt der Kündigung auch nicht mehr als fünf „Altarbeitnehmer“ im Sinne des § 23 Abs. 1 <br />
Satz 2 KSchG bei der Beklagten beschäftigt worden seien. <br />
 <br />
Die Kündigung sei auch nicht aus anderen Gründen rechtsunwirksam. Nach dem zuletzt <br />
unbestrittenen Vortrag der Beklagten sei bei ihr ein Betriebsrat nicht gebildet gewesen. <br />
Ebenso habe die Klägerin nicht bestritten, dass die Beklagte alle Arbeitnehmer gekündigt habe, sodass auch eine Sozialauswahl entsprechend der Rechtsprechung <br />
des BAG zur „Sozialauswahl“ im Kleinbetrieb nicht durchzuführen gewesen sei. <br />
 <br />
Die Kündigung sei auch nicht deshalb treuwidrig gewesen, weil die Klägerin erst im Sommer von der R… B… W… Hotelbetriebsgesellschaft mbH, in der <br />
sie Kündigungsschutz hatte, in die Beklagte übergewechselt sei. Die Beklagte sei nicht verpflichtet gewesen, der Klägerin bei Abschluss des Arbeitsvertrages mitzuteilen, <br />
ob auf sie das Kündigungsschutzgesetz Anwendung finde. Sollte die Klägerin vor Übertritt in die Beklagte nicht ausreichend informiert worden sein, mache dies nicht die Kündigung treuwidrig. Sollte ein Betriebsübergang vorgelegen haben, wäre die Klägerin nicht schutzlos gestellt, da sie, falls tatsächlich eine fehlerhafte Information vorgelegen haben sollte, dem Betriebsübergang noch immer hätte widersprechen können. Ein solcher Widerspruch sei bislang jedoch nicht ausgeübt worden. </p>

<p>Das Urteil hat in der Berufung gehalten (2 Sa 765/10) .</p>

