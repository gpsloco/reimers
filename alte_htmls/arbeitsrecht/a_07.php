			<h2>Fristlose Kündigung und Diebstahl</h2>
			
			
			

<p>Zunehmend verfallen Arbeitgeber darauf, dass sie unliebsame Mitarbeiter schnell und einfach ohne Abfindungszahlung loswerden, wenn eine Straftat, ein kleiner Diebstahl, eine Untreue (Spesenrechnung&#8230;!) ö. ä. vorliegt. Dabei werden auch Kleinstdelikte, die jede Staatsanwaltschaft als Bagatelle sofort ohne Geldstrafe einstellen würde, zu Straftaten hochstilisiert.</p>

<p>So auch hier:</p>

<p>Der Mitarbeiter hatte gem. einer jahrelangen Tradition in einem Unternehmen mit mehr als 600 Mitarbeitern aus der Küche im Einverständnis mit dem Koch ein Pfund Kaffee und etwa 10 Kiwis zu einer kleinen Teilabteilung gebracht, wo sich ebenfalls eine kleine Küche befand. Dort wurde der Kaffee üblicherweise aufgebrüht und von ihm und mehreren anderen Mitarbeitern getrunken. Weil er merkte, dass irgend etwas nicht stimmte, er verfolgt wurde und ihm die Sache komisch vorkam, stellte er den Eimer mit dem Pfund Kaffee und den Kiwis unter eine Treppe im öffentlichen zugänglichen Bereich.</p>

<p>Dies alles gab er in einem späteren Gespräch unumwunden zu. Trotzdem wurde ihm nach jahrelanger Mitarbeit fristlos gekündigt.</p>

<p>Das Arbeitsgericht Schweinfurt hielt die Kündigung für berechtigt.</p>

<p>Das Landesarbeitsgericht Nürnberg setzte - wie es Gerichte gerne tun - einen Vergleich durch, nachdem die Vorwürfe nicht weiter erhoben wurden, wobei es deutlich in die Richtung erklärte, dass solche Fälle zunehmen würden und nach seinem Dafürhalten ein Diebstahl nicht vorlag. Meine Argumentation sah wie folgt aus:</p>

<p>Dem Kläger wird vorsätzlicher Diebstahl unterstellt. Diese Diebstahl wurde erstinstanzlich schlicht behauptet. Er wurde nie untersucht oder nachgewiesen.</p>

<p>Entscheidend ist, dass der Gewahrsamsbruch nicht vorlag. Die Rechtssprechung geht dann von Gewahrsamsbruch aus, wenn beispielsweise in einem Selbstbedienungsladen der Täter Sachen in die Tasche steckt. Hier ging es um ein Pfund Kaffee und 10 Kiwis. Beides befand sich die gesamte Zeit im Zugriffsbereich der beklagten AG. Die Gegenstände wurden offen in einem Eimer transportiert. Die nach § 242 StGB geforderte Wegnahme setzt aber den Bruch fremden und die Begründung neuen eigenen Gewahrsams voraus. Selbst wenn man nicht von einer Einwilligung der Beklagten - die hier Jahre lang vorlag - ausgeht, liegt dennoch kein Fall der Begründung eines neuen Gewahrsams vor. Dies wäre dann der Fall gewesen, wenn die Gegenstände das Gebäude verlassen hätten.</p>

<p>Mit Abstellen der Gegenstände an der Treppe, liegt jedenfalls Rücktritt vom Versuch vor, selbst wenn man einen Gewahrsamsbruch unterstellt.</p>

<p>Ausdrücklich hat der BGH die Gewahrsamserlangung bei Kaffee in vier Plastiktüten und Zigarettenstangen verneint ( BGH STV 84, Seite 376 und OLG Düsseldorf, NJW 86, Seite 2266 ). In den Entscheidungen wurde darauf abgestellt, dass in einem Selbstbedienungsladen bei dem im Einkaufskorb befindlichen Waren, kein Gewahrsamsbruch vorliegt. Der Kläger hätte also hier die Kiwis essen und den Kaffee trinken müssen, um einen Gewahrsamsbruch zu begehen. Dieser Ansicht wollte das Gericht folgen, woraufhin der Vergleich geschlossen wurde ( AZ: 5 Sa 626/04 ).</p>

