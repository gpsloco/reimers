(BAG, Urt. v. 22.01.2004 – 2 AZR 237/03 in NJW 2004, 1818)</p>

<p>Zusammenrechnung der Mitarbeiter in mehreren Geschäften des Arbeitgebers (Filialunternehmen/Einzelhandelsketten) </p>

<p>Es besteht in Rechtsprechung und Literatur zwar weitgehend Einigkeit darüber, dass die Kleinbetriebsklausel des § 23 Kündigungsschutzgesetz betriebsbezogen zu prüfen ist, doch gibt es Bemühungen, zumindest zu einer Arbeitgeber bezogenen Prüfung zu gelangen.<br />
Als Betrieb ist auch im Sinn des § 23 die organisatorische Einheit, innerhalb derer ein Arbeitgeber mit seinen Arbeitnehmern durch Einsatz technischer und immaterieller Mittel bestimmte arbeitstechnische Zwecke fortgesetzt verfolgt, die sich nicht in der Befriedigung des Eigenbedarfs erschöpfen, zu betrachten.<br />
Für Einzelhandelsunternehmen mit zentral gelenkten Verkaufsstellen, in denen jeweils weniger als 10 Arbeitnehmer arbeiten, ist inzwischen anerkannt, dass nicht die jeweilige Verkaufsstelle, sondern die Gesamtheit der Verkaufsstellen den Betrieb bilden, so dass die Anzahl der Arbeitnehmer in allen Geschäften darüber entscheidet, ob das Kündigungsschutzgesetz anwendbar ist.<br />
„Unterhält ein Arbeitgeber mehrere, jeweils nur mit fünf oder weniger Angestellten besetzte, aber einheitlich und zentral gelenkte Verkaufsstellen, so ist nicht schon die einzelne Verkaufsstelle, sondern erst die Gesamtheit aller Verkaufsstellen zusammen mit der zentralen Verwaltungsstelle ein Betrieb im Sinn des Kündigungsschutzgesetzes.“<br />
Bundesarbeitsgericht, Urteil vom 26.08.1971; Arbeitsgerich Solingen, Urteil vom 28.05.2008, 3 Ca 154/08</p>

<p>Sozialauswahl auch im Kleinbetrieb</p>

<p>Das Bundesverfassungsgericht hat bereits 1998 entschieden, dass auch in solchen Betrieben, die den Schwellenwert des § 23 Abs. 1 Satz 2 KSchG nicht erreichen, Arbeitnehmern gegenüber Kündigungen ein gewisser Schutz zustehe. Dieser Schutz sei durch die Anwendung der Generalklauseln des Zivilrechts und unter Beachtung des objektiven Gehalts der Grundrechte im Sinn eines Mindestschutzes zu gewähren. Dieser Rechtsprechung kommt nunmehr besondere Bedeutung zu, da der Gesetzgeber den Schwellenwert für die Anwendung des Kündigungsschutz- gesetzes bekanntlich zum 01.01.2004 auf 10 Arbeitnehmer erhöht hat, das Kündigungsschutz- gesetz mit seinem allgemeinen Kündigungsschutz also nur noch anwendbar ist, wenn mehr als 10 Arbeitnehmer beschäftigt werden.</p>

<p>Das Bundesverfassungsgericht hat in zwei Beschlüssen vom 27.01.1998 ( 1 BvL 15/87 und 1 BvL 22/93, NJW 1998, 1475 u. 1478) ausgeführt, die über die Jahre mehrfach geänderte Kleinbetriebsklausel des § 23 Kündigungsschutzgesetz sei in verfassungskonformer Auslegung nicht zu beanstanden.<br />
Der verfassungsrechtliche Schutz des Arbeitsplatzes in Verbindung mit dem Sozialstaatsprinzip gebiete aber ein gewisses Maß an sozialer Rücksichtnahme, soweit unter mehreren Arbeitnehmern hinsichtlich der Kündigung eine Auswahl zu treffen ist. Ein durch langjährige Mitarbeit erdientes Vertrauen in den Fortbestand eines Arbeitsverhältnisses dürfe nicht generell unberücksichtigt bleiben. Arbeitnehmer müssten vor allem vor willkürlichen oder auf sachfremden Motivern beruhenden Kündigungen geschützt werden. <br />
In zwei neueren Entscheidungen hat das Bundesarbeitsgericht diese wenig praxistauglichen Formulierungen nunmehr konkretisiert und diesem Kündigungsschutz “zweiter Klasse” Konturen gegeben.</p>

<p>Nach Bundesarbeitsgericht hat der Arbeitgeber bei einer beabsichtigten betriebsbedingten Kündigung im Kleinbetrieb, also beabsichtigtem Personalabbau, keine vollkommene Entscheidungsfreiheit. Der Arbeitgeber muss die Auswahl unter den Arbeitnehmern vielmehr nach sachlichen Kriterien vorzunehmen. <br />
Klagt ein gekündigter Arbeitnehmer mit der Begründung, die Auswahlentscheidung des Arbeitgebers verstoße gegen Treu und Glauben, so muss er allerdings, darlegen und beweisen, dass die Kündigung treuwidrig ist. Es reicht aber zunächst aus, wenn der Arbeitnehmer die Sozialdaten der aus seiner Sicht vergleichbaren Arbeitnehmer darlegt. Dann muß sich der Arbeitgeber zum Vortrag des Arbeitnehmers erklären, wenn er die Behauptungen entkräften will (BAG NZA 2001, 833). Aus dem Vorbringen des Arbeitnehmers muss sich allerdings auch ergeben, dass er mit den nicht gekündigten Arbeitnehmern auf den ersten Blick vergleichbar ist (BAG, Urt. v. 06.02.2003 - 2 AZR 672/01 - NJW 2003, 2188).<br />
Arbeitnehmer haben auch dann Chancen, sich gegen eine Kündigung erfolgreich zur Wehr zu setzen, wenn das Kündigungsschutzgesetz auf ihr Arbeitsverhältnis keine Anwendung findet. Willkürliche Kündigungen mit auf sachfremden Motiven beruhend sind unwirksam.</p>

