			<h2>Kündigungsschutz auch im Kleinbetrieb?</h2>
			
			
			

<p>Die Anwendbarkeit des Kündigungsschutzgesetzes ist für Arbeitnehmer und Arbeitgeber häufig eine ausgesprochen wichtige Frage. Denn wenn kein Kündigungsschutz nach dem Kündigungsschutzgesetz besteht, dann kann der Arbeitgeber grundsätzlich das Arbeitsverhältnis jederzeit kündigen. Die Kündigung ist dann wirksam, wenn sie nicht gegen zivilrechtliche Generalklauseln verstößt, also sich z.B. als treuwidrig darstellt. Dieser Schutz ist sehr viel schwächer ausgeprägt als das Kündigungsschutzgesetz.<br />
Der erste Abschnitt des Kündigungsschutzgesetzes, der die maßgeblichen Regelungen enthält, gilt gemäß § 23 Abs. 1 Satz 2 Kündigungsschutzgesetz nur für Betriebe und öffentliche Verwaltungen, in denen in der Regel mehr als 10 Arbeitnehmer unter Ausschluss der Auszubildenden beschäftigt werden.<br />
Dies gilt allerdings unter bestimmten Voraussetzungen nicht für Arbeitnehmer, die am 31.12.2003 nach der damaligen Regelung (Schwellenwert mehr als 5 Arbeitnehmer) bereits Kündigungsschutz genossen. Diesen steht auch heute noch der volle Kündigungsschutz jedenfalls dann zu, wenn die mehr als 5 &#8220;Altarbeitnehmer&#8221; nach wie vor beschäftigt werden.<br />
Bei der Berechnung des Schwellenwerts nach § 23 I 2 KSchG ist der gekündigte Arbeitnehmer auch dann mit zu berücksichtigen, wenn Kündigungsgrund die unternehmerische Entscheidung ist, den betreffenden Arbeitsplatz nicht mehr neu zu besetzen. </p>

