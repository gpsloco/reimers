			<h2>Kündigung des Arbeitnehmers - Vertragsstrafe für ihn?</h2>
			
			
			

<p>Vertragsstrafe - Verlängerung der Arbeitnehmerkündigungsfristen</p>

<p>Urteil des Landesarbeitsgerichts Sachsen-Anhalt vom 22. August 2007 - 4 Sa 118/07 </p>

<p>Das Landesarbeitsgericht hat seine Entscheidung im Wesentlichen wie folgt begründet: Der Kläger (Arbeitnehmer) sei zur Zahlung einer Vertragsstrafe nicht verpflichtet, da die Regelung in § 11 des Arbeitsvertrages einer Inhaltskontrolle nach § 307 Abs. 1 BGB nicht standhalte. Die Verlängerung der Kündigungsfrist für den Kläger durch § 13 Abs. 1 des Arbeitsvertrages stelle sich in der Zusammenschau mit der Vertragsstrafenregelung als „Überraschungsklausel“ dar, die gegen § 305c Abs. 1 BGB verstoße. Im Bereich niedriger Entlohnung für wenig qualifizierte Arbeitnehmer bedeute eine solche Vertragsgestaltung eine erhebliche Benachteiligung, die den Wechsel des Arbeitsplatzes bei Einhaltung einer mehrmonatigen Kündigungsfrist in der Regel unmöglich mache. Für Tätigkeiten mit einem Stundenlohn von 6,00 Euro würden neue Arbeitskräfte nur kurzfristig gesucht. Es sei in der Tat kaum anzunehmen, dass ein neuer Arbeitgeber mehrere Monate auf einen Arbeitnehmer in diesem Bereich warte, wenn dieser an eine längere Kündigungsfrist gebunden sei. Es dürfte daher keinen Zweifeln begegnen, dass für den Kläger eine solche Klausel eine überraschende Regelung darstelle, die nicht wirksam Bestandteil des Arbeitsvertrages geworden sei. Der Kläger habe nicht damit rechnen müssen, dass die Vertragsstrafe auch dann verwirkt werde, wenn er die gesetzliche Kündigungsfrist wahre. Durch § 11 Abs. 2 Satz 2 des Arbeitsvertrages werde die Höhe der Vertragsstrafe ausdrücklich auf „das in der gesetzlichen Mindestkündigungsfrist“ zu zahlende Arbeitsentgelt beschränkt. Dies verdeutliche, dass an sich nur die Verletzung der gesetzlichen Mindestkündigungsfrist durch Zahlung einer Vertragsstrafe sanktioniert werden sollte. Anderes sei überraschend.</p>

<p><br />
Folgendes fand sich im Arbeitsvertrag der Parteien:</p>

<p>&#8220;Vertragsstrafe<br />
        <br />
Nimmt der Mitarbeiter die Arbeit nicht oder verspätet auf, verweigert er vorübergehend unberechtigt die Arbeit, löst er das Arbeitsverhältnis ohne Einhaltung der maßgeblichen Kündigungsfrist unberechtigt auf oder wird der Arbeitgeber durch vertragswidriges Verhalten des Arbeitnehmers zur außerordentlichen Kündigung veranlasst, so hat der Arbeitnehmer an den Arbeitgeber eine Vertragsstrafe zu zahlen. Als Vertragsstrafe wird für den Fall der verspäteten Aufnahme der Arbeit, der vorübergehenden Arbeitsverweigerung und der Auflösung des Arbeitsverhältnisses ohne Einhaltung der maßgeblichen Kündigungsfrist ein sich aus der Bruttomonatsvergütung nach vorstehendem § 4 Abs. 1 zu errechnendes Bruttotagegeld für jeden Tag der Zuwiderhandlung vereinbart, insgesamt jedoch nicht mehr als das in der gesetzlichen Mindestkündigungsfrist ansonsten zu zahlende Arbeitsentgelt. Im Übrigen beträgt die Vertragsstrafe ein Bruttomonatsentgelt gemäß vorstehendem § 4 Absatz 1.<br />
        <br />
Das Recht des Arbeitgebers, einen weitergehenden Schaden geltend zu machen, bleibt unberührt.<br />
Beendigung des Arbeitsverhältnisses<br />
        <br />
Nach Ablauf der Probezeit kann das Arbeitsverhältnis beiderseits ordentlich unter Einhaltung der für den Arbeitgeber nach § 622 BGB gesetzlich geltenden Kündigungsfristen gekündigt werden.<br />
        <br />
Das Recht zur außerordentlichen Kündigung aus wichtigem Grund bleibt unberührt. Eine außerordentliche Kündigung gilt gleichzeitig vorsorglich als fristgerechte Kündigung zum nächstmöglichen Zeitpunkt.&#8221;</p>

<p><br />
Mit Schreiben vom 14. August 2006 kündigte der Kläger (Arbeitnehmer) das Arbeitsverhältnis „fristgemäß“ zum 15. September 2006. Mit Schreiben vom 25. August 2006 und vom 1. September 2006 wies ihn die Beklagte darauf hin, dass die Kündigungsfrist wegen der sechsjährigen Betriebszugehörigkeit zwei Monate zum Monatsende betrage und forderte ihn auf, seine Arbeitsleistung über den 15. September 2006 hinaus zu erbringen, was der Kläger unterließ.</p>

<p>In der Folgezeit erstellte die Beklagte Lohnabrechnungen bis zum 15. September 2006, zahlte jedoch das Nettoentgelt nur abzüglich der von ihr geltend gemachten Vertragsstrafe iHv. 960,00 Euro (6,00 Euro pro Stunde x 8 Stunden x 5 Tage x 4 Wochen) aus. Die Zahlung einer Leistungs- und Qualitätsprämie iHv. 293,00 Euro brutto lehnte sie ab.</p>



