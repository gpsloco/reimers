			<h2>Scheinselbständigkeit freier Mitarbeiter</h2>
			
			
			

<p>Das Landesarbeitsgericht hat in einem Beschluß vom 21.12.98 AZ 5Ta211/98 hierzu Bemerkenswertes entschieden:<br />
dem Beschluß lag ein Beschluß des Arbeitsgerichtes Schweinfurt zugrunde, in dem ein Rechtsstreit an das Amtsgericht Schweinfurt verwiesen wurde, weil ein freier Mirtarbeiter kein Arbeitnehmer im Sinne des Arbeitsrechts sei. Diese Entscheidung hob das Landesarbeitsgericht aufgrund meiner sofortigen Beschwerde auf, und entschied antragsgemäß.Ich hatte insoweit vorgetragen,daß der Kläger einen festen Tagesplan und Mindestarbeitszeiten einzuhalten hatte. Der Kläge mußte auch Urlaub beantragen.Das Landesarbeitsgericht grenzt zwischen freiem Mitarbeiter und Arbeitnehmer ab, indem es nicht die getroffenen vertraglichen Vereinbarungen, sondern die tatsächliche Durchführung des Vertrages bewertet. Aus der praktischen Handhabung ließen sich Rückschlüsse darauf ziehen, von welchen Rechten und Pflichten die Parteien in Wirklichkeit ausgegangen sind. Nicht Arbeitnehmer sei, wer seine Tätigkeit und seine Arbeitszeit im wesentlichen frei gestalten könne. Hier sei dann Arbeitnehmereigenschaft anzunehmen, wenn der Kläger jeweils um 8.00 Uhr seinen Dienst habe antreten müssen, und er bis 16.30 Uhr im Büro habe anwesend sein müssen. Weitere Indizien seien Pausenzeiten von 12.00 Uhr bis 13.00 Uhr und das Beantragen von Urlaub. Schließlich sei auch von Bedeutung, daß der Kläger habe darauf achten müssen, einen ordnungsgemäß aufgeräumten Schreibtisch zu hinterlassen.</p>

<p>In diesem Zusammenhang scheint mir das neue Gesetz (Neue Regelung ab 1. Januar 1999) zur Scheinselbständigkeit von Bedeutung, das zum Zeitpunkt der obigen Entscheidung noch nicht galt. Danach wird ein Beschäftigungsverhältnis vermutet, wenn zwei der vier folgenden Kriterien erfüllt sind:<br />
1. Der Selbständige beschäftigt außer Familienangehörigen keine versicherungspflichtigen Arbeitnehmer<br />
2. er ist in der Regel und im Wesentlichen nur für einenAuftraggeber tätig<br />
3. er erbringt für Arbeitnehmer typische Arbeitsleistungen, unterliegt insbesondere Weisungen des Auftraggebers und ist in dessen Arbeitsorganisation eingegliedert<br />
4. 4. er tritt nicht unternehmerisch am Markt auf
</p>



 

				
				
