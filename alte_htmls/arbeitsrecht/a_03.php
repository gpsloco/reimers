			<h2>20 Jahre Betriebszugehörigkeit und keine Abfindung?</h2>
			
			
			

<p>Die Klägerin wurde 1991 durch die Beklagte eingestellt. Die Beklagte firmierte unter verschiedenen GmbHs, da die Boutique immer wieder umbenannt wurde. Der Eigentümer blieb jeweils der gleiche.</p>

<p>Gleichzeitig betrieb die Inhaberin der Beklagten unter einer anderen Firma quasi den Großhandel zur Boutique.</p>

<p>Insgesamt beschäftigte die Beklagte mehr als 10 Arbeitnehmer. </p>

<p>Hierauf kam es in diesem Fall aber nicht unbedingt an, da die Klägerin schon vor Beschränkung des Kündigungsschutzgesetzes auf 10 Arbeitnehmer ( früher 5<br />
Arbeitnehmer ) eingestellt worden war.</p>

<p>Gleichwohl entließ jetzt die Beklagte die Klägerin nach 20 Jahren Tätigkeit ohne großen Federlesens.</p>

<p>Hiergegen habe ich Klage erhoben und die Arbeitnehmer genannt. Das Arbeitsgericht Würzburg wies die Klage kurzerhand ab. In der (später aufgehobenen) Entscheidung heißt es:</p>

<p>„Die Kündigung ist durch dringende betriebliche Erfordernisse im Sinne von<br />
§ 1 Abs. 2 S. 2 KschG bedingt. Ein Fehler bei der Sozialauswahl ist nicht erkennbar.</p>

<p>aa. Es liegen ausreichende betriebsbedingte Kündigungsgründe vor.</p>

<p>Die Klägerin bestreitet den diesbezüglichen Sachvortrag der Beklagten nicht.<br />
Durch die am 11.07. getroffene Unternehmerentscheidung der Geschäftsführerin ist der Arbeitsplatz der Klägerin weggefallen. Es unterliegt der freien Entscheidung der Beklagten, wie diese ihren Betrieb organisiert. Da die Entscheidung getroffen wurde, das Geschäft mit einer Kraft weniger als früher zu besetzen und da die Geschäftsführerin sich entschieden hat, selbst einen Tag pro Woche mehr zu arbeiten, entfällt der Beschäftigungsbedarf für eine Arbeitskraft an vier Tagen pro Woche.</p>

<p>bb. Ein konkreter Fehler bei der Sozialauswahl hat die darlegungs- und beweispflichtige Klägerin nicht aufgezeigt. Die Klägerin hat in der Klageschrift die Beklagte berechtigterweise gem. § 1 Abs. 2 S. 2 KSchG aufgefordert, die Gründe anzugeben, die zu der getroffenen sozialen Auswahl geführt haben“.</p>

<p>Hiergegen habe ich erfolgreich Berufung eingelegt. Dies mit folgenden Gründen:</p>

<p>Das Arbeitsgericht Würzburg verkennt, dass die Klägerin bei der Firma ....... GmbH, vertreten durch die selbe Geschäftsführerin, dass die Klägerin, als Teilzeitkraft beschäftigt war. Dieses Arbeitsverhältnis ist bis heute ungekündigt. </p>

<p>Auch in materieller Hinsicht ist das Urteil unzutreffend, denn das Arbeitsgericht Würzburg geht irrtümlich davon aus, dass dringende betriebliche Erfordernisse vorlagen. Zudem sei die Sozialauswahl nach Meinung des Arbeitsgerichts nicht fehlerhaft.</p>



<p>Richtig ist, dass </p>

<p>a. keine betrieblichen Erfordernisse vorlagen. Die Gründe der Beklagten waren vorgeschoben. Es handelt sich um vermeintliche Umstrukturierungsmaßnahmen.<br />
Die konkreten Angaben der Beklagten zum Änderungsbedarf und deren Auswirkungen sind nicht schlüssig. Hinzu kommt, dass Verwandte von Mitarbeitern vorgeblich €&nbsp; 400,&#8212;Jobs angenommen haben, in Wahrheit aber gar nicht tätig waren.</p>

<p>Ein nachvollziehbares Organisationskonzept der Beklagten ist nicht erkennbar. </p>

<p>b. Eine soziale Auswahl fand nicht statt. Die anderen Vollzeitkräfte hätten ebenfalls gekündigt werden können. Sie wurden aber bei der Sozialauswahl gar nicht berücksichtigt. Auch die 20-jährige Betriebszugehörigkeit wurde nicht berücksichtigt.<br />
Ebenso der Doppelverdienst der anderen Mitarbeiterinnen, alle kinderlos, wurde nicht berücksichtigt. </p>

<p>Im Ergebnis musste das Landesarbeitsgericht Nürnberg nicht entscheiden, da nach meinem Vortrag die Gegenseite auf den von mir telefonisch erklärten Vergleichsvorschlag ohne Abstriche einging und an die Klägerin eine erhebliche Abfindung ausgekehrt werden konnte. Der Vergleich wurde dann am Landesarbeitsgericht geschlossen.</p>

