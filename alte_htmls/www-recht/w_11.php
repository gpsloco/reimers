<p><b>Neue Entscheidung des Landgerichts:</b></p>

<p>Das Landgericht hat in seinem Antrag vom 16.05.02, AZ: 4 O 106/00 mit der Verpflichtung eines Homepage-Betreibers seine Seite regelmäßig auf rechtswidrige Einträge zu kontrollieren zu befassen und entschied:<br />
Der Betreiber ist verpflichtet den Inhalt des Gästebuchs seiner Homepage regelmäßig auf rechtswidrige Eintragung zu überprüfen und letztere ggf. zu löschen.</p>

<p></b>Allgemeines:</b></p>

<p>Nachdem ursprünglich die Rechtssprechung alleine vom Prioritätsprinzip ausging, also dem die Domain beließ, der sie zuerst angemeldet hatte, hat sich das Bild hierzu inzwischen nahezu vollständig gewandelt.<br />
Grundsätzlich verpflichten die Gerichte - dies auf verschiedenen Wegen - den Domaininhaber, die Domain herauszugeben, wenn der Name eines Namensträgers oder eines Unternehmens vorhanden ist.</p>

<p>Dies gilt immer dann, wenn es sich bei dem Namen um den Namen einer Person oder um den Namen einer Stadt ( LG Duisburg, in MMR 2000, S. 168 ) handelt, leider auch oft, wenn es sich um den Namen eines Unternehmes geht.<br />
a. So wurde in der sog. Krupp-Entscheidung gegen den Namensinhaber der Fa. Krupp und für das Unternehmen Krupp entschieden ( NJW/CoR 1998, S. 175 ). Hr. Krupp hatte sich die Domain Krupp sichern lassen. Das Unternehmen Krupp Hösch AG beanspruchte für sich die gleiche Domain. Im Recht der Namensgleichheit sei der Grundsatz zu beachten, dass niemand an einem redlichen Gebrauch seines eigenen Namens im Wirtschaftsfeld gehindert werden dürfe.<br />
Gleiches wurde im Fall Shell entschieden ( OLG München, CR 1999, S. 382 ). Auch hier wurde die Privatperson verurteilt, sich durch einen Namenszusatz vom Namen des Mineralölkonzerns abzusetzen.<br />
b. In einer Entscheidung des OLG Bremen ging es um die Frage, ob aus einer eingetragenen Wortmarke Rechte auf eine Domain hervorgehen.<br />
Den Prozeßkostenhilfeantrag des Markeninhabers wies das Landgericht ab. Es verwies allerdings gleichzeitig darauf hin, dass Markenrechte hier dem Domaininhaber vorgehen würden.( LG Bremen, Az.:12 O 87/01)<br />
c. Dies gilt auch G für erweiterte Kennzeichen. So hat das OLG München entschieden, dass durch die Domainbezeichnung Rolls Royce Börse.de das Markenrecht von Rolls Royce verletzt sei ( OLG München, MMR 2000, S. 104 ).<br />
d. Das OLG München geht in einer Entscheidung vom 06.04.00, AZ: 6 U 4123/99 ( BRP S. 775 ) sogar soweit, dass es eine Markenbenutzung dann unterstellt, wenn im nichtsichtbaren Teil der Internetwebseite als Metatag ( als Suchwort ) eine Marke verwendet werde.</p>





 

				
				
