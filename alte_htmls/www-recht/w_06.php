			<h2>Haftung für Links</h2>
			
			
			

<p>							<br />
Links als solche sind vom Gesetzgeber (außer § 5 Abs. 1 TDG) nicht geregelt. Deshalb ist man letztlich von der Auffassung(-sgabe), Kenntnis und oftmals nicht zuletzt von der Willkür einzelner Richter abhängig.</p>

<p>Zum Teil gibt es ermutigende Rechtssprechungen, aber eben nur zum Teil. <br />
Unterschieden werden muss zwischen gewerblichen und nichtgewerblichen Homepages, www-sites, blogs oder was auch immer. Bei Gewerblichen ist die Gefahr der Haftung für Links bei weitem größer. So hat das LG Stuttgart, AZ: 17 O 706/11 am 11.11.11 (auch wenn es so wirkt, bei dem Urteil handelt es sich nicht um einen Faschingsscherz) im Wege der einstweilige Verfügung entschieden:</p>

<p>Tenor</p>

<p>1. Dem Antragsgegner wird es bei Vermeidung eines vom Gericht für jeden Fall der Zuwiderhandlung festzusetzenden Ordnungsgeldes bis zu 250.000,00 Euro, ersatzweise Ordnungshaft, oder Ordnungshaft bis zu sechs Monaten,<br />
untersagt,<br />
unter der Internetdomains ... werbefinanzierte Links zu den Themen ... bereit zu halten und/oder bereithalten zu lassen.</p>

<p>2. Der Antragsgegner trägt die Kosten des Verfahrens.</p>

<p>3. Die Wirksamkeit dieser Beschlussverfügung hängt davon ab, dass die Antragstellerin dem Antragsgegner zugleich mit einer Ausfertigung dieses Beschlusses eine beglaubigte Abschrift der Antragsschrift samt Kopien der Anlagen zustellt.<br />
Streitwert: 75.000,00 Euro.</p>

<p><br />
Dabei kam es dem Gericht nicht darauf an, ob der Domaininhaber von den Links wusste (oder nicht), da er als Inhaber der Domain verantwortlich für den abrufbaren Inhalt ist. Zudem handelt er, ob willentlich oder nicht, auch im geschäftlichem Verkehr. Das stellte bereits 2007 das OLG Hamburg fest (Urt. v. 08.02.2007 – Az.: 408 O 37/06)<br />
In dem Hamburger Fall wurde eine Markenrechtsverletzung verneint, weil nach Ansicht des Gerichts keine Verwechslungsgefahr bestand. Dennoch erkannte das Gericht, dass das Anbieten von “sponsored Links” eine Dienstleistung ist, der Domaininhaber deshalb in geschäftlichem Verkehr handelt, mithin für Markenrechtsverletzungen haftbar ist. Dafür reicht es aus, wenn die Domain einer Marke zum verwechseln ähnlich ist und werbefinanzierte Links eingeblendet werden, die auch noch in den Dienstleistungsbereich des Markeninhabers fallen.</p>

<p><br />
Zur Privatnutzung dagegen hat das LG Braunschweig ( 05.10.2011, Az.: 9 O 1956/11) entschieden:</p>

<p>Das Setzen eines Hyperlinks ist stets unproblematisch, wenn der Inhalt auf den verwiesen wird, zulässig ist. Trotz rechtswidrigen Inhalts vermag das Setzen des Hyperlinks zulässig sein, wenn ein überwiegendes Informationsinteresse besteht und der Verfasser sich den verlinkten Inhalt nicht zu Eigen gemacht hat.</p>





 

				
				
