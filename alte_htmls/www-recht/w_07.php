Unverlangt zugesandte e-mail Werbung:<br />
Die Rechtssprechung zu unverlangt zugesandten e-mails ist - wie es so schön heißt - uneinheitlich.<br />
Dies überrascht einigermaßen, nachdem bei unverlangt zugesandten Telefaxen bzw. Telefonanrufen ganz überwiegend davon ausgegangen wird, dass solche unzulässig ist ( Urteil BGH WM 2000, S. 1264 ). Anders soll dies nur bei Gewerbetragenden sein, bei denen von einem mutmaßlichen Einverständnis ausgegangen werden kann.<br />
Für rechtlich unzulässig halten e-mail Werbung:<br />
- Landgericht Traunstein ( NJW-CoR 1997, S. 494 )<br />
- Amtsgericht Brake ( NJW 1998, S. 3209 )<br />
- Landgericht Berlin ( CR 1998, S. 623 )<br />
