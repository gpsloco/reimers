			<h1 id="top">Thilo Reimers Rechtsanwalt, Dipl. Volkswirt, Würzburg</h1>
			
			<h2>Keine GEMA Gebühren für die Hochzeit</h2>
			
			
			

<p>Das türkische Hochzeiten Großveranstaltungen sind, wusste ich. Das da allerdings auch die GEMA mitmischt war mir neu. Was war passiert? <br />
	<br />
		Unstreitig hat der Beklagte am am 27.05.2006 für die Hochzeitsfeier die ...-Hallen in Dortmund angemietet und eine Liveband hat während der Veranstaltung Musik gespielt. Streitig ist, ob dies eine öffentliche Wiedergabe darstellt.<br />
Deshalb macht die Klägerin Schadensersatzansprüche in Höhe von 262,47 EUR wegen einer öffentlichen Musikwiedergabe mit Musikern bei der Hochzeitsfeier des Sohnes der Bekl. geltend.</p>

<p>		Nach § 15 Abs. 3 UrhG ist eine Wiedergabe öffentlich, wenn sie für eine Mehrzahl von Personen bestimmt ist, es ei denn, dass der Kreis dieser Personen bestimmt abgegrenzt ist und sie durch gegenseitige Bezie­hungen oder durch Beziehung zum Veranstalter persönlich untereinander verbunden sind. Dabei ist der Begriff der persönlichen Verbundenheit nicht eng im Sinne nur familiärer oder freundschaftlicher Beziehungen zu verstehen. Entscheidend ist auf den engen gegenseitigen Kontakt abzustellen, der bei den Beteiligten das Bewusstsein hervorruft, persönlich miteinander verbunden zu sein, vgl. LG Oldenburg, GRUR-RR 2006, 177f. Ob eine solche persönliche Verbundenheit besteht, ist im wesentlichen eine Tatfrage, wobei derjenige, der sich auf diesen Ausnahmetatbestand beruft, darlegungs- und beweispflichlig ist, hier der Beklagte.<br />
		Unter Berücksichtigung dieser Grundsätze ist das Gericht aufgrund der Beweisaufnahme davon überzeugt, dass bei der Hochzeitsfeier vom 27.05.2006 Musik nicht öffentlich wiedergegeben worden ist.<br />
		Der Zeuge ... hat glaubhaft bekundet, dass an der Feier ca. 600 Gäste teilgenommen haben. Die Hälfte sei von seiner Familie, die andere Hälfte sei von der Familie der Braut eingeladen worden. Dabei habe es sich um Verwandte und Freunde, aber auch etwa um Nachbarn gehandelt. Jeder Gast habe zuvor eine schriftlicheEinladung erhalten. Bei der Veranstaltung selbst habe von jeder Familie eine Person die eintreffenden Gäste in der Halle begrüBt und auf diese Weise auch die Besucher kontrolliert. Nachdem sämtliche Gäste eingetroffen sei en, seien diese Kontrollen eingestellt worden. Außer den geladenen Gästen habe niemandan der Feier teilgenommen, jedenfalls sei ihm nicht bekannt, dass sich eine fremde Person in der Halle aufgehaltenhabe.<br />
		Der Zeuge ... hat letztlich keine abweichenden Angaben gemacht. Der Zeuge konnte nur das wiedergeben, was in dem schriftlichen Kontrollbericht enthalten ist, er hatte aber im Übrigen keine Erinnerung mehr an die Kontrolle selbst. Insoweit stehen die Angaben des Zeugen ... den Bekundungen des Zeugen ... nicht entgegen.<br />
		Wenn aber ausschließlich persönlich eingeladene Gäste an der Veranstaltung teilgenommen haben, um mit dem Brautpaar deren Hochzeit zu feiern, sind die Teilnehmer durch ihre jeweilige Beziehung zum Veranstalter bzw. zum Brautpaar, persönlich untereinander verbunden, und durch diese Verbundenheit und die Eigenart der Veranstaltung wird unter sämtlichen Besuchern das Geruhl erzeugt, an diesem Abend einer in sich geschlossenen Gesellschaft anzugehören.<br />
		Dieser Einordnung als nicht öffentliche Veranstaltung steht es nicht entgegen, wenn keine strengen Einlasskontrollen unter Vorlage der schriftlichen Einladung durchgeruhrt wurden und es dem Zeugen ... ohne weiteres möglich war, die Halle zu betreten und die Feier für einige Minuten vom Eingangsbereich aus zu beobachten. Denn für die Einordnung als öffentliche oder nicht öffentliche Veranstaltung ist nicht das Maß der Kontrollen entscheidend. Es kommt vielmehr auf die persönliche Verbundenheit der Teilnehmer und ihr Gemeinschaftsgefühl an, was nicht dadurch beeinträchtigt wird, dass sich einzelne, fremde Personen unberechtigt Zutritt zu der Veranstaltung verschaffen oder verschaffen können.<br />
		Auch die relativ hohe Zahl der geladenen Gäste rechtfertigt keine abweichende Beurteilung. Wie die verschiedenen ähnlich gelagerten Fälle, die bereits vor dem Amtsgericht Bochum anhängig waren, zeigen, ist eine große Hochzeitsfeier mit hunderten geladenen Gästen im türkischen Kulturkreis nicht unüblich, wobei auch weit entfernte Verwandte und neben engen Freunden auch Bekannte und Nachbarn eingeladen werden. Allein aus diesem Umstand kann aber nicht gefolgert werden, dass unter den Gästen der Hochzeitsfeier nicht das Gefühl und Bewusstsein der persönlichen Beziehung zum Brautpaar besteht.<br />
		Im Ergebnis ist damit davon auszugehen, dass die türkische Hochzeit vom 27.05.2006 eine nicht öffentliche Veranstaltung war, so dass für die hierbei erfolgte Musikwiedergabe ein Gebührenanspruch der Klägerin nicht entstanden ist.<br />
		Die Klage war daher mit der Kostenfolge aus § 91 ZPO abzuweisen.<br />
		Die Entscheidung hinsichtlich der vorläufigen Vollstreckbarkeit folgt aus §§ 708 Nr. 11 , 713 ZPO.</p>

