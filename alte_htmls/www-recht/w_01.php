			<h2>Wann wurde ein Vertrag im Internet geschlossen ?</h2>
			
			
			

<p>Vor kurzem hatte ich das Urteil des Amtsgerichts Leipzig bereits besprochen (siehe:&nbsp; <a href="http://goo.gl/HRO9z">http://goo.gl/HRO9z</a> ).<br />
Dieses Urteil ist auch noch in anderer Hinsicht klärend .</p>

<p>Oftmals und gerne genommen wird das Angebot: kostenlose Dienstleistung für ...<br />
( vom Download des acrobat reader, Rotenplaner bis zur Mitfahrzentrale gibt es hier alles ). Also eigentlich nix mit Vertrag meint man&#8230; ABER:<br />
Tatsächlich findet sich dann irgendwo im Kleingedruckten der Hinweis, dass ein <br />
24-Monatsabo mit einem Jahressalär von €&nbsp; 96,&#8212;bis&#8230; - nach oben sind keine Grenzen gesetzt - mit Nutzung fällig sind. <br />
Firmen bzw, Webadressen, die hier immer wieder auftauchen sind:</p>

<p>routenplanung.de<br />
my-downloads.de<br />
downloadtransfer.de<br />
premiumdownload.de<br />
Mitfahrzentrale-24.de</p>

<p>Dem hat das Amtsgericht mit der vorgenannten Entscheidung einen Riegel vorgeschoben. Dort heißt es nämlich: Zum einen wurde zwischen den Parteien kein Vertrag nach §§ 145 ff. BGB wirksam geschlossen, wonach die Verfügungsbeklagten zur Zahlung einer Vergütung in Höhe von €&nbsp; 96,&#8212;pro Jahr über eine Laufzeit von zwei Jahren verpflichtet ist&#8230; <br />
Maßgeblich ist der objektive Empfänger am Horizont eines durchschnittlichen Nutzers des Internets nach §§ 133, 157, BGB.</p>

<p>Die Angabe über den Preis für die Nutzung des Internetangebots des Verfügungsbeklagten ist nicht Bestandteil eines etwaigen Vertrages geworden, denn sie ist am rechten Rand unter dem fällt „Schnäppchenforum“ und über dem Feld „aktuelle Informationen“ plaziert, an einer Stelle, an der der Besucher der Internetseite nicht damit rechnen muss. Im Hauptteil der Seite sind die Nutzerdaten einzugeben, darunter befindet sich eine Zeile, in der ein Haken dafür zu setzen ist, dass die AGB und die Datenschutzerklärung akzeptiert werden. Darunter befindet sich ein sehr großes farblich hervorgehobenes Feld mit der Aufschrift „Jetzt anmelden“.<br />
Ein durchschnittlicher Besucher, der an dem Angebot der Verüfungsbeklagten Interesse hat, gibt zunächst seine Daten ein, setzt den Haken für die Akzeptanz der AGB und er Datenschutzbestimmung und klickt auf das Feld „Jetzt anmelden“.<br />
Der am rechten Rand unauffällig im Fließtext plazierte Hinweis auf die Kostenpflichtigkeit des Angebots wird dabei in der Regel nicht zur Kenntnis genommen.</p>

<p>Diese ( gemeint ist Kostenpflichtigkeit ) hätte die Verfügungsbeklagte wenigstens deutlich hervorheben müssen, um eine wirksame Einbeziehung zu erreichen. Selbst wenn ein Vertrag zwischen den Parteien zustande gekommen wäre, wäre dieser nach §§ 312b, 312g, 355 BGB wirksam widerrufen worden. Der Verfügungsklägerin steht ein Widerrufsrecht nach § 312d<br />
Abs. 1, S. 1 BGB zu, denn es würde sich bei dem Vertrag um einen Vertrag nach <br />
§ 312b, Abs. 1, S. 1 zwischen einem Verbraucher ( § 13 BGB ) und einem Unternehmer ( § 14 BGB ) handeln und der Widerruf ist nicht nach § 312d IV. und nicht V. BGB ausgeschlossen. Die Verfügungsbeklagte hat den Widerruf rechtzeitig erklärt, denn die Widerrufsfrist hatte mangels ordnungsgemäßer Belehrung gemäß §§ 312d Abs. 2, § 355 Abs. 2, S. 1 BGB noch nicht begonnen ( vgl. §§ 355<br />
Abs. 2, S. 3 BGB ). Es liegt keine deutlich gestaltete Belehrung vor, denn das Deutlichkeitsgebot ist verletzt. Erforderlich ist, dass sich die Belehrung durch Farbe, größere Buchstaben, Fettdruck oder Sperrschrift in einer nicht zu übersehenden Weise <br />
aus dem übrigen Text ergibt ( BGH NJW 1996, S. 1964; 2009, S. 3020, 3022 ). Diese Anforderungen sind bei Weitem nicht erfüllt. Auf der Seite mit dem Formular zur Eingabe der persönlichen Daten und dem Button zur Anmeldung selbst befindet sich keine Widerrufsbelehrung. Lediglich in der Fußleiste befindet sich ein Hyperlink<br />
( elektronischer Verweis ) mit dem Namen „Widerrufsrecht“. Dieser Hyperlink ist in einer Reihe von 9 weiteren Hyperlinks unauffällig plaziert und in keiner Weise gegenüber den anderen Hyperlinks hervorgehoben. Bei einer solchen Anordnung kann die Widerrufsbelehrung sehr leicht übersehen werden.</p>

<p>Das Urteil ist relativ klar gefasst. Nach meinem Dafürhalten ließe es sich noch deutlich vereinfachen. Es geht letztlich immer darum, dass die sog. Essentialia eines Vertrages <br />
( also die Essenz, das Wichtigste des Vertrages ) deutlich werden. Dies sind fast immer <br />
Kaufpreis, Kaufgegenstand und Parteien. Auch im Internet müssen diese Essentialia klar erkennbar sein.&nbsp; </p>

<p>Vorsichtig sollte in diesem Zusammenhang der Gewerbetreibende sein. Er ist nicht ähnlich geschützt wie ein Verbraucher. Zum allgemeinen Unverständnis ist es so, dass die dauernd versendeten Adressangebote ( Ihre Firma wird in unserem fantastischen Adresssverzeichnis kostenlos geführt. Bitte überprüfen Sie, ob die Angaben zur Adresse und Namen korrekt sind&#8230; ) gerichtlich sanktioniert werden. Denn auch dort (z : B. <br />
Gewerbeauskunfstzentrale.de, GWE, Wirtschaftsinformations GmbH )<br />
findet sich im Kleingedruckten dann, dass das Ganze sehr teuer wird. Bei den erhöhten Anforderungen an Gewerbetreibenden finden sich immer wieder Gerichte, die Betreiber solcher Angebote Geld zusprechen ( AG Frankfurt a. M., AZ: 31 C 2696/07-16, AG Dieburg, AZ: 29 C 67/07 ).</p>

