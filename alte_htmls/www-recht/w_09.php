<p>Örtliche Zuständigkeiten:<br />
Örtlich zuständig ist im Rahmen des Wettbewerbsrechts bei Domainstreitigkeiten jedes angegangene Gericht.<br />
Das AG München entschied in diesem Zusammenhang für Kaufrecht ( 23.10.00, AZ: 213 C 13044/00 ), dass Geschäfte, die im Internet mit gegenseitigen Pflichten abgeschlossen werden, nicht ortsgebunden sei. Für diesen Fall greift dann der allgemeine Gerichtsstand.
