			<h2>schutzrechtsfähige Domainnamen</h2>
			
			
			

<p>Für allgemeine Gattungsbegriffe und kennzeichnende Begriffe entschied der BGH soeben ( 16.05.01 ), dass solche Begriffe als Domainnamen schutzrechtsfähig sind.<br />
Anders als im Markenrecht, in dem allgemeine Begriffe, wie Zigaretten, Taschentücher, Markt o.ä. nicht schutzrechtfähig sind, da insoweit ein Freihaltungsbedürfnis bestehe, gebe es im Internet für diese Begriffe ke in Freihaltungsbedürfnis. Entschieden wurde dies für die im Fall der Mitwohnzentrale. de, die diesen Begriff nach der Entscheidung des BGH nunmehr führen darf. Durch einen englischen Mitwettbewerber im Englischen praktisch identischen Namen war die Mitwohnzentrale auf Unterlassung verklagt worden.</p>





 

				
				
