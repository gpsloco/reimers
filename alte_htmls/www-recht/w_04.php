			<h2>Facebook Like Button</h2>
			
			
			

<p>Das Kammergricht Berlin hat hierzu in einem einstweiligen Verfügungsverfahren entschieden ( Beschluss vom 29.04.2011, AZ: 5 W 88/11 ).</p>

<p>Die Parteien vertrieben Waren und Dienstleistungen über das Internet, sog.<br />
„Sterntaufen“. Der Antragsgegner hat auf seine Webseite den &#8220;like it&#8221; Button installiert.<br />
Wenn ein User den Button nutzt, werden Daten vom Besucher an Facebook weitergeleitet.</p>

<p>Die Antragsstellerin monierte, dass die Benutzer der Webseite nicht ausdrücklich auf die damit verbundene Datenübertragung informiert würden. Sie sah darin einen Wettbewerbsverstoß, der im einstweiligen Verfügungsverfahren angegangen wurde.</p>

<p>Sowohl Landgericht als auch Kammergericht Berlin haben die einstweilige Verfügung abgelehnt.</p>

<p>Dabei wurde zwar ein Verstoß gegen § 13 TMG bejaht. Facebook könne IP-Adresse<br />
und Nutzerdaten zuordnen und weiter verarbeiten. Damit liege an sich ein datenschutzrechtlicher Verstoß vor ( damit wären wohl auch Bußgelder der Datenschutzaufsichtsbehöre möglich ).</p>

<p>Eine Abmahnung nach UWG sei gleichwohl nicht möglich, da § 4 Nr. 11 UWG nicht erfüllt sei. Im Rahmen dieser Norm sei es unerheblich, ob ein Normverstoß einen Wettbewerbsvorteil bringe. Als Beispiel wird der BGH zitiert ( 1 ZR 152/07 ) wonach beispielsweise das Hinterziehen von Steuern kein Wettbewerbsverstoß sei.</p>

<p>Im Ergebnis halte ich die Verneinung von UWG-Verstößen ( in Betracht käme auch<br />
§ 3 Abs. 1 UWG oder § 4 Nr. 2 UWG ) für fragwürdig. Man kann das so sehen, man muss es aber nicht.</p>

<p>Ich gehe davon aus, dass in diesem Bereich noch weitere Entscheidungen anderer Oberlandesgerichte ergehen werden. </p>





 

				
				
