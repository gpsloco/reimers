			<h2>Keine Nachschusspflicht in geschlossenen Immobilienfonds</h2>
			
			
			

<p>Kammergericht Berlin</p>

<p>Ein geschlossener Immobilienfonds macht gegen seine Fondmitglieder eine Nachschusspflicht in ganz erheblichem Umfang geltend. 1995 zahlten die - heute - beklagten Fondmitglieder DM 424.000,&#8212;ein. Jetzt will der Fonds weitere € 122.000,&#8212;nebst Zinsen.<br />
Das Landgericht Berlin sprach dem Fonds den Anspruch zu. Das Kammergericht hob diese Entscheidung auf und urteilte, dass keine Nachschusspflicht bestehe.</p>

<p>Das für die Fondmitglieder eigentlich ärgerliche ist, dass der Baupreis „überraschend hoch“  war. Üblicherweise beträgt der qm Wohnungseigentum ganz grob geschätzt € 2.000,&#8212;. Hier wurden etwa € 3.900,&#8212;verbaut ohne das besondere Luxusaufwendungen oder sonstiges ersichtlich wären. Jetzt soll zudem noch weiter nachgeschossen werden. Dies ausgehend von einem Sanierungsplan, der wieder von relativ hohen Mieteinnahmen ausgeht. Ähnliche Pläne hat der Fonds ja bereits 1995 vorgelegt, die offensichtlich falsch waren.</p>

<p>Der Senat erklärte in der mündlichen Verhandlung vom 05.11.07 folgendes:</p>

<p>Der Gesellschaftsvertrag zum Fond stelle keine Grundlage für eine Nachschusspflicht dar. Deshalb seien besonders hohe Anforderungen an einem solchen gesellschaftsrechtlichen Beschluss zu stellen. Diese seien nicht erfüllt. </p>

<p>Insbesondere fragwürdig sei, ob der neue Sanierungsplan langfristig saniere, da in ihm Annahmen getroffen seien, die zweifelhaft erscheinen ließen, ob nicht zukünftig den Gesellschaftern auch wieder Nachteile entstünden. Auch sei die behauptete Entlastung im Außenverhältnis ( zur Bank keine Haftung ) nicht endgültig, da im Innenverhältnis ( die anderen Gesellschafter ) keine Entlastung erfolge und mithin weitere Belastungen möglich seien.</p>

<p>Weiterhin sei ein solcher Beschluss die ultimo ratio, da die Treuepflicht der Gesellschafter aufs Höchste angespannt werde. Hier sei aber vorher z. B. ein Kapitalerhöhungsverfahren möglich gewesen. In einem Kapitalerhöhungsverfahren würden die Gesellschafter, die der Sanierung zustimmen und Gelder geben, an der Gesellschaft mehr oder höher beteiligt, als diejenigen, die der Kapitalerhöhung nicht zustimmten und nicht einzahlten.</p>

<p>Schließlich kam noch hinzu, dass aufgrund eines zwischen landgerichtlicher Entscheidung und Verhandlung des Kammergerichts getroffenen Gesellschafterversammlung, neue Beschlüsse aktuell waren, die das Sanierungskonzept obsolet machten.<br />
Insgesamt eine erstmal erfreuliche Entscheidung für alle von Nachschusspflichten betroffenen Gesellschafter bzw. Mitglieder eines geschlossenen Immobilienfonds. Mit dieser Entscheidung wird es den Gesellschaften nicht mehr so einfach gemacht, von ihren Mitgliedern Nachschüsse in erheblicher Höhe einzufordern. Von dieser Entscheidung dürften einige Gesellschaften betroffen sein. Die Lage der geschlossenen Immobilienfonds stellt sich mitunter schwierig dar, da mit Fondsgründung Annahmen für die zukünftige Entwicklung des Immobilienmarkts getroffen wurden, die de facto niemals erreichbar waren. &#8221; Sichere&#8221; Geldanlagen fürs Alter wurden auf diesem Weg reihenweise vernichtet.</p>

