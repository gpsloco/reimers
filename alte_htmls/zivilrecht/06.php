			<h2>Urlaub von der Scheidung</h2>
			
			
			

<p>Nachdem so eine Scheidung ganz schön anstrengend sein kann und in meinem Fall für meinen Mandanten auch war, machte er Urlaub.</p>

<p>Was er nicht wusste war:<br />
Seine holde Ehefrau schickte ihm den Privatdetektiv hinterher, dies in der wahnwitzigen Vermutung, dass mein Mandant im Urlaub Geld verdienen würde. Von diesen zusätzlichen Geldeinnahmen erhoffte sie sich einen erhöhten Unterhaltsanspruch. Natürlich kam dabei nichts raus - außer einer Rechnung des Privatdetektivs über<br />
€ 11.500,01.</p>

<p>Man mag es kaum glauben:</p>

<p>Aber im Kostenfestsetzungsverfahren meinte das Amtsgericht Würzburg, dass die <br />
€ 11.500,01der Privatdetektivrechnung Kosten des Gerichtsverfahrens seien. Die Kosten<br />
wurden wie Anwalts- und Gerichtskosten ( die vergleichsweise nur einen Bruchteil ausmachten ) festgesetzt, dies mit dem Ergebnis, dass mein Mandant, der den Prozess überwiegend gewonnen hatte, immer einen noch erheblichen Teil hätte zahlen müssen.<br />
Ein teurer Urlaub&#8230;</p>

<p>Glücklicherweise sah das OLG Bamberg ( AZ: 7 WF 266/08 ) die Sache anders. Die Gründe im folgenden:</p>

<p>Das Amtsgericht/Familiengericht Würzburg hat mit Urteil vom 12.03.2008 in Ziffer 4 eine Kostenentscheidung dahingehend getroffen, dass der Beklagte 1/4, die Klägerin 3/4 der Kosten des Rechtsstreits trägt.<br />
Gegen den am 15.10.2008 zugestellten Beschluss hat der Beklagte am 22.10.2008 Beschwerde eingelegt.</p>

<p>Die Beschwerde ist begründet.</p>

<p>Der Streit der Parteien geht alleine um die Frage, ob von der Klägerseite geltend gemachte Detektivkosten in Höhe von € 11.500,01 notwendig waren und damit erstattungsfähig sind.<br />
Detektivkosten sind nur erstattungsfähig, wenn sie sich, gemessen an den wirtschaftlichen Verhältnissen der Parteien unter Bedeutung des Streitgegenstandes, in vernünftigen Grenzen halten, prozessbezogen waren, die erstrebten Feststellungen wirklich notwendig waren und die Ermittlungen nicht einfacher und/oder billiger erfolgen konnten. Es kommt dabei nicht darauf an, ob die Ermittlungen des Prozessausgang beeinflusst haben, sondern ob eine vernünftige Partei berechtigten Grund gehabt hätte, einen Detektiv einzuschalten (Zöller/Herget, ZPO, 27. Auflage, RdNr. 13 zu § 91 ZPO<br />
„Detektivkosten“, m.w.N.)<br />
Diese Voraussetzungen sind vorliegend nicht gegeben. Nach den Gründen des Urteils vom 12.03.2008 ging es um die Frage von Einkünften des Beklagten aus selbständiger Musikertätigkeit. Der Detektiv war beauftragt, mögliche Konzerttermine der Band des Beklagten zu ermitteln. Nach seinen eigenen Angaben stützen sich seine Recherchen hauptsächlich auf Internetauftritte der Band „Velvet Underground“ sowie auf Nachfragen.</p>

<p>Zur Ermittlung des Einkommens des Beklagten war die Beauftragung eines Detektivs nicht notwendig. Dazu standen die Mitglieder der Band als Zeugen zur Verfügung. Die Internetauftritte hätten auch von der Partei selbst recherchiert werden können.<br />
Die Klägerin hat mit Beschluss vom 19.12.2007 Prozesskostenhilfe ohne Ratenzahlung bewilligt bekommen. Das Einkommen des Beklagten wurde vom erstinstanzlichen Gericht mit € ... festgestellt.<br />
Die Klägerin hatte erstinstanzlich einen Ehegattenunterhalt in Höhe von monatlich<br />
€ 337,32 bzw. € 291,83 Kindesunterhalt in Höhe von € 315,&#8212;/ € 384,&#8212;und monatlich<br />
€ 289,49 an Mehrbedarf geltend gemacht.<br />
Detektivkosten in Höhe von € 11.500,01 sind vorliegend gemessen an den wirtschaftlichen Verhältnissen der Parteien unter Bedeutung des Streitgegenstandes als unvernünftig zu beurteilen. Darüber hinaus war zur Feststellung des Einkommens des Beklagten die Einschaltung eines Detektivs nicht notwendig.</p>

<p>Zur erneuten Kostenfestsetzung im Hinblick auf die beiderseits gestellten Kostenanträge war der Beschluss vom 07.10.2008 aufzuheben und die Sache zur erneuten Entscheidung an das Amtsgericht Würzburg zurückzuverweisen.</p>

<p>Bamberg, 18.12.2008</p>

