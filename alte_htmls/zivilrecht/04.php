			<h2>Unterbrechung der Verjährung</h2>
			
			
			

<p>Ein Mahnbescheid unterbricht dann die Verjährung nicht, wenn der Anspruch unzureichend bezeichnet ist, so der BGH. </p>

<p>In der Entscheidung war ein Steuerberater gegen seinen Mandanten vorgegangen<br />
( was darauf schließen lässt, dass das Verhältnis unerfreulich gewesen sein muss&#8230; ).<br />
Der Mandant hatte erklärt, dass ihm die Rechnungen des Steuerberaters nicht zugegangen seien.</p>

<p>Gleichwohl kam das Oberlandesgericht zu dem Ergebnis, dass der Mahnbescheid die Verjährung unterbreche.</p>

<p>Dem widersprach nun der BGH und erklärte, dass die Kennzeichnung von Ansprüchen so unterscheidbar sein muss, dass sie Gurndlage eines der materiellen rechtskraftfähigen Vollstreckungstitels sein können und der Schuldner zudem beurteilen können müsse, in welchem Umfang er sich zur Wehr setzen wolle. Bei der Bezeichnung des Anspruchs kann auf Rechnungen oder Schriftstücke Bezug genommen werden. Diese müssen aber - und das war hier entscheidend - dem Mandanten zugegangen sein. Denn nur, wenn ein solches Schriftstück dem Schuldner bekannt ist, braucht es den Mahnbescheid nicht in Abschrift zur Verfügung gestellt werden. Insbesondere verwarf der BGH die Annahme des Berufungsgerichts, dass eine Nachfrageobliegenheit des Schuldners bestehe. Keinesfalls sei der Schuldner bei der Vergütung seines Steuerberaters gehalten, sich nach Grund und Höhe der angemahnten Vergütung zu erkundigen.</p>

<p>BGH IX ZR 160/07 
</p>



 

				
				
