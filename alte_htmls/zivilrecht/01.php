			<h2>Drohung der Weitergabe an Schufa rechtswidrig</h2>
			
			
			

<p>Das AG Leipzig, Az: 118 C 10105/09<br />
hat in der Entscheidung vom 13. Januar 2010 Klartext in Sachen Schufa gesprochen. Danach dürfen Daten nicht einfach so an die Schufa weitergegeben werden - erst recht nicht, wenn der Betroffene (hier Verfügungsklägerin) widerspricht. Im einzelnen:</p>

<p>In dem Rechtsstreit (...)<br />
hat das Amtsgericht Leipzig auf Grund der mündlichen Verhandlung beschlossen:</p>

<p>1. Die Verfügungsbeklagte (Absenderin einer letzten MAHNUNG) hat die Kosten des Rechtsstreits zu tragen.  <br />
2. Der Streitwert wird auf 2.500 € festgesetzt.<br />
...<br />
Am 01.12.2009 mahnte die Verfügungsbeklagte die Verfügungsklägerin und übersendete ihr am 15.12.2009 ein Schreiben, das mit „LETZTE MAHNUNG“ überschrieben war und zur Zahlung von 96 € zzgl. 5 € Mahngebühren aufforderte. Darin hieß es: „Bitte haben Sie Verständnis, dass wir im Sinne einer wirtschaftlichen Abwicklung unserer Vertragsverhältnisse den weiteren Einzug einem darauf spezialisierten Inkasso-/Rechtsanwaltsbüro übertragen werden. Dadurch entstehen Ihnen weitere Kosten und gegebenenfalls weitere Nachteile wie z.B. ein negativer Schufa-Eintrag.“ Der Text war ab „Inkasso-/Rechtsanwaltsbüro“ fett hervorgehoben.   Noch am 15.12.2009 übersendete die Verfügungsklägerin der Verfügungsbeklagten ein Telefax, in dem sie auf ihr Schreiben vom 17.11.2009 Bezug nahm und jede Zahlung verweigerte. Außerdem widersprach sie der Weitergabe personenbezogener Daten an die Schufa-Holdung AG<br />
...<br />
Die Verfügungsklägerin muss eine Weitergabe ihrer Daten an die Schufa-Holding AG nicht dulden, denn die Verfügungsbeklagte ist zur Datenübermittlung nicht berechtigt, so das Gericht. Eine Datenübermittlung würde nur in berechtigter Weise erfolgen, wenn die Verfügungsklägerin hierzu entweder wirksam ihre Einwilligung erklärt hätte (§§ 4 I, 4a I BDSG) oder die Übermittlung durch § 28 I 1 Nr. 2, III 1 Nr. 1 BDSG gerechtfertigt wäre. Diese Voraussetzungen sind nach zutreffender Ansicht der Gerichts nicht erfüllt. &#8220;Die Verfügungsklägerin hat mit Schriftsatz vom 15.12.2009 die Weitergabe von personenbezogenen Daten an Dritte der Verfügungsbeklagten untersagt. Falls eine wirksame Einwilligung i.S.v. §§ 4 I, 4a I BDSG in die Datenweitergabe überhaupt erteilt worden war, wäre diese jedenfalls dadurch widerrufen worden. Denn eine Einwilligung in die Datenübermittlung kann jederzeit mit Wirkung für die Zukunft widerrufen werden.  Ferner liegen die Voraussetzungen für eine Datenübermittlung nach § 28 I 1 Nr. 2, III 1 Nr. 1 BDSG nicht vor, denn eine Abwägung der widerstreitenden Interessen ergibt, dass das schutzwürdige Interesse der Verfügungsklägerin an einer Unterlassung der Weitergabe das Interesse der Verfügungsbeklagten wie auch etwaiger Dritter an einer Weitergabe überwiegt.  Fraglich ist es bereits, ob eine Datenübermittlung an die Schufa-Holding AG oder andere Wirtschaftsinformationsdienste vor einer rechtskräftigen Entscheidung über die streitgegenständliche Forderung rechtmäßig ist, wenn der Betroffene Einwendungen geltend macht, die nicht offensichtlich unbegründet sind, und der Betroffene nicht bereits in der Vergangenheit unberechtigt die Zahlung verweigert oder unberechtigte Einwendungen geltend gemacht hat (vgl. hierzu OLG Düsseldorf, MMR 2007, 387, 388; AG Elmshorn, CR 2005, 641).   Gegen die Zulässigkeit der Datenübermittlung ist anzuführen, dass anderenfalls jemand, der Rechte für sich in Anspruch nimmt, mit der Ankündigung einer Schufa-Meldung angesichts der großen Bedeutung des Schufa-Registers Druck ausüben und somit den Bedrohten zur Zahlung auch auf unberechtigte Forderungen bewegen könnte.   Sinn des Schufa-Systems ist aber der Schutz der Wirtschaftsteilnehmer vor zahlungsunfähigen oder zahlungsunwilligen Schuldnern, nicht aber die Durchsetzung möglicherweise unberechtigter Forderungen. Den Gläubigern soll nicht eine allgemeine Drohkulisse zur Verfügung gestellt werden, bei der das Schufa-System zu reinen Inkassozwecken missbraucht werden würde.<br />
