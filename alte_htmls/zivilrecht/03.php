			<h2>Unterhaltspflicht entfällt wenn höheres Einkommen verschwiegen wird</h2>
			
			
			

<p>Entscheidung des OLG Bamberg, AZ: 7 UF 125/09<br />
Verpflichtung den Kläger ungefragt über die wesentliche Erhöhung des Einkommens zu informieren</p>

<p>1. Auf die Berufung des Klägers wird das Endurteil des Amtsgerichts Würzburg<br />
vom 21.04.09 abgeändert. Ziffer 1. des dort geschlossenen Vergleichs wird dahingehend abgeändert, <br />
dass der Kläger ab 01.11.08 keinen nachehelichen Unterhalt mehr zu leisten hat.</p>

<p>2. Die Berufung der Beklagten gegen das Endurteil des Amtsgerichts Würzburg wird zurückgewiesen.</p>

<p>3. Die Beklagte hat die Kosten des Rechtsstreits in beiden Instanzen zu tragen.</p>

<p>Gründe:</p>

<p>						I.</p>

<p>Die Parteien sind geschiedene Eheleute. Der Kläger begehrt die Abänderung eines im Verfahren AZ: 4 F 1169/01 Amtsgericht/Familiengericht Würzburg am 24.10.06 abgeschlossenen Vergleichs über nachehelichen Unterhalt der Beklagten in Höhe von<br />
€&nbsp; 400,&#8212;monatlich ab 01.11.08 dahingehend, dass er nicht mehr zu Unterhaltszahlungen verpflichtet ist.</p>

<p>Das Erstgericht hat die Unterhaltspflicht des Klägers mit Rücksicht auf ein von der Beklagten verschwiegenes höheres Eigeneinkommen im Jahr 2007 auf die Dauer eines Jahres vom 01.11.08 bis 31.10.09 ausgesetzt und die Klage im Übrigen abgewiesen. </p>

<p>Hiergegen richtet sich die Berufung des Klägers, mit der er sein Ziel weiterverfolgt, <br />
ab 01.11.08 dauerhaft von der Unterhaltspflicht befreit zu werden. Die Berufung der Beklagten zielt auf eine vollständige Zahlung des Unterhalts.</p>

<p>Die Parteien streiten, wie schon in erster Instanz, um die Frage, wie die gemeinschaftliche Immobilie unterhaltsrechtlich zu behandeln ist, welche Belastungen des Klägers unterhaltsrechtlich anzuerkennen sind, sowie darum, ob die Beklagte nicht durch Verschweigen von Einkommen im Jahr 2007 durch einen Vollstreckungsversuch aus einem nicht mehr vollstreckbaren Titel, über Getrenntlebensunterhalt im Jahre 2009 oder durch eine eigenmächtige Alleinnutzung der gemeinschaftlichen Immobilie ihren Unterhaltsanspruch dauerhaft verwirkt hat.</p>

<p>						II.</p>

<p>Die Berufung des Klägers erweist sich als begründet, die der Beklagten als unbegründet. </p>

<p>Der Unterhaltsanspruch der Beklagten ist gem. § 1579 Nr. 5 BGB verwirkt mit der Folge, dass der Kläger - jedenfalls, wie beantragt - ab 01.11.08 keinen nachehelichen Unterhalt mehr zahlen muss. </p>

<p>Nach §&nbsp; 1579 Nr. 5 BGB ist ein Unterhaltsanspruch zu versagen oder zeitlich zu begrenzen, soweit die Inanspruchnahme des Verpflichteten auch unter Wahrung der<br />
Belange eines dem Berechtigten zur Pflege oder zur Erziehung anvertrauten gemeinschaftlichen Kindes grob unbillig wäre, weil der Berechtigte sich über schwerwiegende Vermögensinteressen des Verpflichteten mutwillig hinweggesetzt hat.<br />
Im zugrundeliegenden Vergleich vom 24.10.06 sind die Parteien vom Einkommen der Beklagten in Höhe von €&nbsp; 207,&#8212;ausgegangen. Tatsächlich hat die Beklagte in der Folgezeit im Laufe des Jahres 2007 ausweislich des Steuerbescheides €&nbsp; 17.700,&#8212;brutto verdient. Dies entspricht einem Nettoeinkommen von monatlich wenigstens<br />
€&nbsp; 1.055,&#8212;.</p>

<p>Nach dem Sachverhalt hat der Kläger erst nach Erlass des Steuerbescheids im Zusammenhang mit der Auseinandersetzung um den Nachteilsausgleich wegen des durchgeführten Realsplittings erfahren. </p>

<p>Die Beklagte war nach Abschluss des Vergleichs verpflichtet den Kläger ungefragt über die wesentliche Erhöhung ihres Einkommens zu informieren.</p>

<p>Um die Pflicht zur Rücksichtnahme der einen Partei auf die Belange der anderen Partei erhöht sich, wenn es um die Durchführung einer Unterhaltsvereinbarung geht.<br />
In einem solchen Fall ist der Unterhaltsberechtigte im Hinblick auf seine vertragliche Treuepflicht gehalten, jederzeit und unaufgefordert dem anderen Teil Umstände zu offenbaren, die ersichtlich dessen Verpflichtungen aus dem Vertrag berühren<br />
( BGH 19.05.99 XII ZR 210/97 und BGH 29.01.97 XII ZR 257/95 in FamRZ 1997<br />
S. 483 ). </p>

<p>Angesichts des Umstandes, dass sich das Nettoeinkommen der Beklagten in etwa verfünffacht hat, hätte es sich ihr auch als Laie aufdrängen müssen, dass die Zahlung eines Unterhalts von €&nbsp; 400,&#8212;nicht mehr ihren aktuellen Einkommensverhältnissen entspricht. Unter Zugrundelegung der übrigen Grundlagen des Vergleichs errechnet sich nämlich nur noch ein Anspruch von etwa €&nbsp; 110,&#8212;. </p>

<p>Die Beklagte kann nicht damit gehört werden, dass sie im Jahr 2007 jeweils nur befristete Arbeitsverhältnisse hatte, verbunden mit der Unsicherheit, ob diese verlängert würden und ob sie die Arbeitsbelastung auch gesundheitlich würde durchstehen können. Die erste Befristung ging zunächst über sechs Monate, wie die Beklagte in ihrer Anhörung selbst eingeräumt hat. Spätestens nach Ablauf des ersten Monats hätte nach Ansicht des Senats auch unter Würdigung der Unsicherheiten ein Anlass zur ungefragten Information des Klägers bestanden, weil die Beklagte dann ihrer gesundheitliche Belastbarkeit als voraussichtlich ausreichend hätte bewerten können und müssen, und von einer nachhaltigen Einkommenserhöhung noch über mehrere Monate hinweg hätte ausgehen können und müssen.</p>

<p>Die Beklagte kann auch nicht damit gehört werden, dass sie Ende des Jahres 2007 bei einer Anhörung im Rahmen des Prozesskostenhilfeverfahrens zum Verfahren wegen Getrenntlebensunterhalt ihre aktuellen Einkommensverhältnisse mit dem Richter<br />
- in Abwesenheit des Klägers und wohl auch in Abwesenheit von dessen Prozessbevollmächtigten - erörtert hat. Hierdurch hat sie ihre Informationspflicht gegenüber dem Kläger nicht erfüllt, insbesondere nicht unverzüglich. </p>

<p>Auch im Jahr 2008 hatte die Beklagte unstreitig ein Nettoeinkommen von €&nbsp; 717,&#8212;, das mehr als zweimal so hoch war, wie in dem Vergleich zugrundegelegtes Einkommen.<br />
Im Rahmen der Prüfung der groben Unbilligkeit ist genau abzuwägen, ob der Anspruch insgesamt zu versagen, nur herabzusetzen oder zeitlich zu begrenzen ist<br />
( vgl. hierzu im Einzelnen Wendel-Staudigel, das Unterhaltsrecht in der familienrichterlichen Praxis 7. Auflage 2008 Randzeichen 675 b ).<br />
Das Verhalten der Beklagten ist von relativ großer Rücksichtslosigkeit geprägt, weil ihre Einkommenssteigerung evident war. Durch die Pflichtverletzung ist dem Kläger auch ein voraussichtlich dauerhafter Schaden entstanden, weil er im Jahr 2007 einen erheblich zu hohen Unterhalt gezahlt hat. Die Einkommensverhältnisse des Klägers sind auch nicht derart gut, dass ihm der Schaden und das Fortbestehen einer Unterhaltspflicht nicht wesentlich belasten würden. Die Beklagte ist grundsätzlich, was ihr Einkommen aus dem Jahre 2007 belegt in der Lage, ihren angemessenen Bedarf selbst zu bestreiten; ihre behauptete Krankheit und auch ihre jetzige Arbeitslosigkeit sind nicht ehebedingt. Hinzu kommt, dass die Beklagte derzeit einen faktischen Wohnvorteil hat durch die Nutzung der gemeinschaftlichen Immobilie zu Wohnzwecken. Bei einer gemeinschaftlichen Fremdvermietung der Immobilie hätte sie zusätzlich anteilige Einkünfte aus Vermietung und Verpachtung oder aber, wenn das Anwesen veräußert oder ein Übererlös zwischen den Parteien verteilt würde, Kapitaleinkünfte. Auf die mittlerweile volljährige und damit nicht mehr betreuungsbedürftige Tochter braucht bei der Entscheidung keine Rücksicht genommen zu werden. </p>

<p>Diese Umstände führen - insoweit über die Rechtsansicht des Erstgerichts hinausgehend - dazu, dass jedenfalls ab 01.11.08 ein Unterhaltsanspruch der Beklagten ganz entfällt. </p>

<p>Auf die anderen Streitpunkte kommt es nicht mehr an.</p>

<p>						III.</p>

<p>Kostenentscheidung</p>

<p>Die Kostenentscheidung beruht auf § 91 ZPO.</p>

<p>Die Revision war nicht zuzulassen.</p>

<p>gez. PD Dr. Bernreuther, Dr. Meyer und Räth</p>

