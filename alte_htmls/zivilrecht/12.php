			<h2>Verjährung von Gewährleistungsansprüche beim Teilekauf und Einbau</h2>
			
			
			

<p>In der Entscheidung des Landgerichts Würzburg Berufungskammer, AZ: 43 S 57/98 ging es um die Verjährung von Gewährleistungsansprüche beim Teilekauf und Einbau. Insbesondere war fraglich, ob eine Unterbrechnung der Verjährung deshalb statt gefunden hatte, weil die Parteien ein Gutachten einholen wollten. Ob dieses Gutachten eingeholt werden sollte, war umstritten.</p>

<p>Das Gericht wertete den Vertrag als Werkvertrag. Die Gewährleistungsansprüche verjähren gem. § 633, 638 BGB nach einem halben Jahr.</p>

<p>Umstritten waren hier insbesondere Verjährungsbeginn, Abnahme des Werkes und Unterbrechung der Verjährung. Das Gericht urteilte wie folgt:</p>

<p>Endurteil:</p>

<p>Die Berufung des Klägers wird zurückgewiesen.</p>

<p>Gründe&#8230;</p>

<p>Die für den Verjährungsbeginn maßgebliche Abnahme des Werkes ist am 22.7.95 erfolgt. Unter Abnahme gem. § 640 BGB ist die körperliche Annahme des Werkes im Wege der Besitzübertragung verbunden mit der Billigung des Werkes als in der Hauptsache vertragsgemäße Leistung zu verstehen ( NJW 93, 1972 )...</p>

<p>Vorliegend hat der Kläger unstreitig sein Fahrzeug nach der Durchführung von Umbaumaßnahmen bei dem Zeugen F. abgeholt, nachdem ihm mitgeteilt worden war, das Fahrzeug sei fertig. In der bloßen Entgegennahme des umgebauten Fahrzeugs liegt auch nach Auffassung der Kammer noch keine Abnahme im Sinne von § 640 BGB ( OLG Düsseldorf NJW 95, Seite 142 ). Im vorliegenden Fall kommt aber hinzu, dass der Kläger das Fahrzeug weitere vier Tage unbeanstandet genutzt hat. Unstreitig hat der Kläger erstmals am 25.7. den Beklagten angerufen und um eine Rechnung für die Umbauarbeiten gebeten. In der viertägigen Nutzungszeit seit Abholung des Fahrzeugs hätte der Mangel ohne weiteres bekannt werden können, da hierfür wenige Tage des Gebrauchs genügen, zumal der Kläger unmittelbar nach Abholung des Fahrzeugs dieses über eine längere Zeit und eine größere Entfernung von seinem Wohnort überführt hat&#8230;</p>

<p>Die als Zeugin vom Berufungsgericht gehörte geschiedene Ehefrau des Klägers K. war trotz ihres Bemühens um Erinnerung nicht in der Lage hinreichend genaue Angabe über Datengeschehensabläufe und deren zeitliche Einordnung zu machen. Zwar konnte sich die Zeugin an einen Anruf des Klägers beim Beklagten erinnern, indem der Kläger Geräusche und ein Schwimmen des Fahrzeugs gerügt hat, ohne jedoch den Zeitpunkt des Gesprächs näher angeben zu können. Angesichts der während der gesamten Zeugenaussage deutlich erkennbaren Unsicherheit der Zeugin und des nur eingeschränkten Erinnerungsvermögens, das aufgrund des langen Zeitablaufs nicht überrascht, kommt diesem Beweismittel keine entscheidungserhebliche Beweiskraft zu. Nach Auffassung der Kammer liegt es nahe, dass die Zeugin tatsächlich das vom Kläger geschilderte und anlässlich der zweiten Heimfahrt am 12.9.95 bemerkte Brummen und Rumpeln der Vorderachse mit den Vorgängen bei der ersten Heimfahrt am 22.7.95 verwechselt hat.</p>

<p>Mit der Entgegennahme des umgebauten Fahrzeugs und der vorbehaltlosen Zahlung des Restwerklohnes an den Zeugen F. am 22.7.95, sowie der Nutzung des Fahrzeugs in der Folgezeit ohne Mängelrüge, hat der Kläger die streitgegenständliche Werkleistung abgenommen. Hierfür spricht auch, dass der Kläger noch am 25.7.95 lediglich die Rechnung beim Beklagten angefordert hat. Unerheblich in diesem Zusammenhang ist der Umstand, dass der Kläger das Fahrzeug ohne Zahlung nicht erhalten hätte, da er sich in Kenntnis dieser besonderen Umstände eine spätere Abnahme nicht vorbehalten hat.</p>

<p>Die Verjährung der Gewährleistungsansprüche des Klägers ist jedoch gem. § 639 BGB gehemmt, solange der Werkunternehmer im Einverständnis mit dem Besteller das Werk nach Vorhandensein von Mängeln untersucht, die gerügten Mängel zu beseitigen versucht oder ein Sachverständiger im beiderseitigen Einvernehmen der Parteien tätig wird ( OLG Köln, NJW RR 1995, Seite 692 ). Die Hemmung der Verjährung endet jedoch dann, wenn der Werkunternehmer das Ergebnis seiner Prüfung dem Besteller mitgeteilt, ihm gegenüber die Mängel für beseitigt erklärt oder die Fortstetzung der Mängelbeseitigung abgelehnt hat ( § 639 II BGB ).</p>

<p>Spätestens mit Schreiben vom 9.10.95, das unstreitig dem Kläger am 10.10.95 zugegangen ist, hat der Beklagte dem Kläger mitgeteilt, dass eine werkvertragliche Haftung des Beklagten nicht in Betracht kommt. Hierin ist eine endgültige Leistungsverweigerung des Beklagten gem. § 639 II BGB zu sehen&#8230;</p>

<p>Somit ist spätestens mit Zugang des Schreibens des Beklagten am 10.10.95 die Hemmung der Verjährung beendet worden mit der Folge, dass gem. § 205 BGB der Zeitraum währenddessen die Verjährung gehemmt war, in die Verjährungsfrist nicht einzurechnen ist.</p>

<p>Zu einer erneuten verjährungshemmenden Prüfung der Mängel im Sinne von § 639 II BGB ist es auch nicht durch die vom Beklagten behauptete Beauftragung des Sachverständigen gekommen. Dies würde voraussetzen, dass die Untersuchung des umgebauten Fahrzeugs des Kläger durch den Sachverständigen im Auftrage des Beklagten oder im Einverständnis der Parteien erfolgt ist&#8230;</p>

<p>Soweit der Kläger behauptet, der Beklagte sei mit der Beauftragung einverstanden gewesen, wird dies vom Beklagten bestritten. Dem für die verjährungshemmenden Tatsachen beweispflichtigem Kläger ist der Beweis für diese Behauptung nicht gelungen. Die von der Berufungskammer gehörte Zeugin K. H. konnte sich weder an einen Sachverständigen, Hr. Funk noch an Einzelheiten des späteren Verhaltens des Beklagten erinnern. Da der Kläger insoweit beweisfällig geblieben ist, scheidet eine erneute Hemmung aus&#8230;</p>

<p>Unterbrochen wurde die Verjährung gem. § 209 BGB erst wieder mit der Klageerhebung.<br />
Klageerhebung am 6.8.96. Zu diesem Zeitpunkt waren aber die Gewährleistungsansprüche verjährt.</p>

<p>Dem Kläger steht auch nicht das geltend gemachte Nutzungsentgelt für die den Nutzungsausfall zu, der durch Umbau- und Reparaturmaßnahmen des Beklagten entstanden ist. Nach § 635 BGB stellt zwar grundsätzlich auch der Verlust der Gebrauchsvorteile einen ersatzfähigen Schaden dar, jedoch sind auch diese Ansprüche bereits verjährt. Bei Nutzungen handelt es sich um abhängige Nebenleistungen, die gem. § 242 BGB spätestens mit dem Hauptanspruch, nämlich dem Gwährleistungsanspruch verjähren ( OLG Köln, NJW 1994, Palandt 58. Auflage, § 224, Randnr. 1 )...
</p>



 

				
				
