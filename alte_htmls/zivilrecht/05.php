			<h2>Kindesunterhalt / Mehrbedarf / Vollstreckung aus altem Urteil bei neuem Sachverhalt</h2>
			
			
			

<p>Das OLG Bamberg hat im  Hinblick auf KIndesunterhalt, genauer gesagt auf Mehrbedarf folgendes Urteil gefällt:</p>

<p>1. Auf die Berufung des Klägers wurde das Urteil des Amtsgerichts Würzburg vom 29.07.09 aufgehoben. </p>

<p>2. Das Urteil des Amtsgerichts - Familiengerichts Würzburg, AZ: 1 F 238/06 wird dahingehend abgeändert, dass ab dem 20.01.2009 der für das gemeinschaftliche Kind zu zahlende Mehrbedarf auf monatlich €&nbsp; 104,57 herabgesetzt wird.</p>

<p>Gründe:</p>

<p>Das Amtsgericht - Familiengericht Würzburg hat mit Urteil vom 29.07.2009 die Abänderungsklage des Klägers zurückgewiesen.</p>

<p>Hinsichtlich der tatsächlichen Feststellung wird auf diese Urteil Bezug genommen.</p>

<p>Gegen das am 12.08.2009 zugestellte Urteil ... der Kläger beantragt das Urteil des Amtsgerichts aufzuheben. </p>

<p>... Die Beklagte beantragt die Berufung zurückzuweisen.</p>

<p>Die Berufung des Klägers ist teilweise begründet.</p>

<p>Die Kosten für den Besuch der Privatschule stellen Kosten des Mehrbedarfs dar ( BGH<br />
FamRZ 1983, S. 48 ). Insofern ist gegenüber dem abzuändernden Urteil vom 12.03.2008 nur insoweit eine tatsächliche Änderung in den örtlichen Gegebenheiten eingetreten, als das Kind statt der Schule Marktbreit die private katholische Volksschule V. in Würzburg besucht. Insoweit handelt es sich prozessual um den selben Mehrbedarf. Die Kosten für die Privatschule betragen monatlich €&nbsp; 125,58 für die beide Parteien anteilig haften.</p>

<p>Die Kosten für eine Heilpraktikerin zur Behandlung von Migräne und Tinitus bei dem Kind sind kein anzuerkennender Mehrbedarf, weil die Behandlung dieser Erkrankung durch einen Mediziner erfolgen, dessen Kosten von der Krankenversicherung abgedeckt werden. Damit können die Rechtsfragen, ob den durch das abzuändernde Urteil zuerkannten Mehrbedarfskosten auch ein anderer Sachverhalt zugrundegelegt werden kann oder dazu eine neue Leistungsklage erforderlich ist, und ob wegen der gemeinsamen elterlichen Sorge der Parteien ein Mehrbedarf geltend gemacht werden kann, der durch eine einseitige Entscheidung eines Elternteils ausgelöst worden ist, der zwar die Obhut für das Kind hat, bei dem es aber um einen Sachverhalt geht der nicht Dinge des täglichen Lebens betrifft, dahingestellt bleiben.</p>

<p>gez. Fuchs <br />
Richter am Oberlandesgericht</p>

<p>In der mündlichen Verhandlung erklärte RiOLG Fuchs, dass er der Meinung sei, dass hinsichtlich der gemeinsamen elterlichen Sorge der Parteien sich weder in der Rechtssprechung noch in der Kammer eine Meinung gebildet habe, in dem Sinne, ob nur gemeinsam eine Entscheidung für den Gang zur Heilpraktikerin notwendig sei. Hinsichtlich des neuen Mehrbedarfs - ursprünglich war Mehrbedarf für Nachhilfestunden gefordert worden, jetzt war dieser ausgetauscht worden in Mehrbedarf für Heilpraktikerrechnungen - meine er, (nachdem er ursprünglich anderer Meinung gewesen war), dass ein neuer Sachverhalt vorliege und deshalb aus dem alten Urteil kein Mehrbedarf hergeleitet und vollstreckt werden könne. </p>

