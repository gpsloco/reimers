			<h2>Strafrecht II: Wie man ein Opfer zum Täter macht (et vice versum)</h2>
			
			
			

<p>Wie man ein Opfer zum Täter macht</p>

<p>Meinem Mandanten wurde auf einem Auge kurzfristig das Augenlicht genommen, der Tränenkanal durchtrennt, die Hornhaut abgeschabt und das Lid abgerissen.<br />
Glücklicherweise sieht er heute wieder ( wenn auch verschleiert ). </p>

<p>Daraufhin habe ich vom Täter ein Schmerzensgeld gefordert. Nachdem der Täter nicht reagierte habe ich Strafanzeige gestellt. Im darauffolgenden Strafverfahren wurde mein Mandant gehört, es tauchten weitere Zeugen auf, nämlich die Ehefrau des Täters und deren langjähriger Freund. Aufgrund deren Aussagen wurde der Täter freigesprochen. <br />
Das muss einen nicht wundern; so etwas passiert; es kommt schon mal vor, dass ein Gericht sich wegen unterschiedlicher Zeugenaussagen auf die Seite der Mehrheit der Zeugen schlägt.</p>

<p>Nun aber begann der Wahnwitz: Die Staatsanwaltschaft klagte meinen Mandanten an, er habe eine falsche Verdächtigung gegenüber dem Täter gemacht. <br />
In dem darauffolgenden Verfahren wurden vor dem gleichen AG Bad Mergentheim nun wieder die Zeugen, Ehefrau und langjähriger Freund, und natürlich der Täter selbst gehört. Es nimmt nicht wunder, wenn diese ihre Aussagen aus der ersten Verhandlung wiederholten. </p>

<p>Allerdings hatte der Täter sich inzwischen im Rahmen einer zivilrechtlichen Verhandlung zur Zahlung eines Schmerzensgeldes verpflichtet. Auch ein von ihm kurz nach der Tat abgesandtes SMS, indem er sich entschuldigt, war als Gegenstand der Verhandlung neu.</p>

<p>Gleichwohl: Richter sind nunmal Menschen, die sich nicht irren. Wenn ich den Täter <br />
freispreche, muss das Opfer ein Täter sein, denn das Opfer hat den Täter offensichtlich zu Unrecht angezeigt (§ 164 StGB)</p>

<p>So geschah es. Das Opfer wurde verurteilt.</p>

<p>In der Berufung wurden nun wieder die gleichen Zeugen gehört - allerdings wurden die Tatsachen der freiwilligen Zahlung von Schmerzensgeld und der Entschuldigung per SMS anders gutiert. <br />
Schlussendlich wurde hier endlich das Opfer nicht mehr zum Täter gemacht, sondern freigesprochen. Das ganze Verfahren hat sich über ein Jahr lang hingezogen. Den Glauben in ein funktionierendes Justizsystem düfte das Opfer weitgehend verloren haben. Aktenzeichen der Berufungsentscheidung: 4 NS 22 Js 2004/09.</p>

<p>Als Anwalt kann man nur froh sein, wenn im Rahmen der andauernden Verkürzung der Rechtswege wenigstens im Strafrech noch eine zweite volle Instanz existiert. Im Verwaltungsrecht ist so eine Berufungsentscheidung nicht denkbar, im Zivilrecht dürfte schon die Zahl der Berufungsverfahren deutlich zurück gegangen sein.
</p>



 

				
				
